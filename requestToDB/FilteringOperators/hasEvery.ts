// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовый поиск по пересечению массивов (Метод hasEvery)
// =========================================================================
// Логика: Оператор 'hasEvery' проверяет, что массив в базе данных содержит 
// АБСОЛЮТНО ВСЕ элементы из переданного списка. Если в базе у поста есть теги 
// ["NestJS", "Prisma"], а мы ищем ["NestJS", "Prisma", "Docker"], то этот пост 
// отсеется, так как компонента "Docker" в нем не хватило.

// Пример вызова: const posts = await findPostsByTagsCombo(["NestJS", "Prisma"])
export async function findPostsByTagsCombo(targetTags: string[]) {
  const posts = await prisma.post.findMany({
    where: {
      content: {
        hasEvery: targetTags, // Массив content должен содержать и "NestJS", и "Prisma" одновременно
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "post-uuid-111",
        "title": "Продвинутый бэкенд",
        "content": ["NestJS", "Prisma", "Postgres"], // Оба искомых тега присутствуют
        "published": true,
        "authorId": "user-uuid-123"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Поиск по пересечению с логическим фильтром (where + hasEvery)
// =========================================================================
// Логика: Совмещаем строгую проверку набора элементов массива с обычными полями. 
// База данных найдет только те публикации, которые открыты для всех (published: true) 
// и содержат в себе обязательную связку технологий "TypeScript" и "Backend".

// Пример вызова: const techPosts = await getPublishedTechPosts(["TypeScript", "Backend"])
export async function getPublishedTechPosts(requiredTags: string[]) {
  const posts = await prisma.post.findMany({
    where: {
      published: true, // Статичное условие
      content: {
        hasEvery: requiredTags, // Только посты, где есть весь список тегов
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "post-uuid-222",
        "title": "Гайд по архитектуре",
        "content": ["TypeScript", "NestJS", "Backend"],
        "published": true,
        "authorId": "user-uuid-123"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Поиск комбо-тегов у конкретного автора (authorId + hasEvery)
// =========================================================================
// Логика: Ищем внутри таблицы постов записи определенного разработчика, 
// у которых в массиве контента или тегов присутствует строго заданный набор 
// хард-скиллов. Полезно для фильтрации портфолио автора.

// Пример вызова: const magoExpertPosts = await getAuthorExpertPosts("user-uuid-123", ["Prisma", "Postgres"])
export async function getAuthorExpertPosts(userId: string, tags: string[]) {
  const posts = await prisma.post.findMany({
    where: {
      authorId: userId, // Фильтр по автору
      content: {
        hasEvery: tags, // Фильтр по полному совпадению набора элементов
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "post-uuid-111",
        "title": "Продвинутый бэкенд",
        "content": ["NestJS", "Prisma", "Postgres"],
        "published": true,
        "authorId": "user-uuid-123"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Выбор между двумя жесткими наборами массивов (hasEvery + OR)
// =========================================================================
// Логика: Повышаем сложность. Сценарий: нам нужно найти посты, которые подходят 
// под жесткий стек ИЛИ фронтендера, ИЛИ бэкендера. 
// Мы создаем два изолированных блока `hasEvery` и объединяем их через массив `OR`.

// Пример вызова: const targetedPosts = await getSpecificStackPosts()
export async function getSpecificStackPosts() {
  const posts = await prisma.post.findMany({
    where: {
      OR: [
        { content: { hasEvery: ["React", "Redux"] } },   // Вариант 1: Полный стек фронта
        { content: { hasEvery: ["Node.js", "Prisma"] } } // Вариант 2: Полный стек бэка
      ],
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-1", "title": "React App", "content": ["React", "TypeScript", "Redux"] },
      { "id": "post-2", "title": "Node API", "content": ["Node.js", "Prisma", "Postgres"] }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Реляционный поиск авторов по полному стеку (hasEvery в подзапросах some)
// =========================================================================
// Логика: Продвинутый уровень. Мы ищем Пользователей (User), но критерием 
// является наличие у них хотя бы одного поста (`some`), который содержит 
// ВЕСЬ запрашиваемый технологический стек. 
// База найдет авторов, написавших полноценные статьи по комплексным темам.

// Пример вызова: const seniorDevs = await findUsersByPostStack(["NestJS", "Prisma", "Postgres"])
export async function findUsersByPostStack(fullStack: string[]) {
  const users = await prisma.user.findMany({
    where: {
      posts: {
        some: {
          content: {
            hasEvery: fullStack, // Проверяем, что в массиве связанного поста есть все три технологии
          },
        },
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 6: Боевой эндпоинт сложной фильтрации контента (hasEvery + Пагинация)
// =========================================================================
// Логика: Максимальная сложность. Реализуем профессиональный поиск по пересечению 
// тегов для каталога статей. Метод фильтрует только опубликованный материал, 
// требует стопроцентного присутствия всех выбранных пользователем тегов, 
// сортирует по дате и отдает данные строго выверенными страницами.

// Пример вызова: const page = await getAdvancedCatalogPage(["Мидл", "Node.js"], 10, 0)
export async function getAdvancedCatalogPage(selectedTags: string[], limit: number, offset: number) {
  const posts = await prisma.post.findMany({
    where: {
      published: true, // 1. Фильтр: Только открытые статьи
      content: {
        hasEvery: selectedTags, // 2. Фильтр: Обязательное наличие ВСЕХ выбранных тегов
      },
    },
    orderBy: {
      createdAt: 'desc', // 3. Сортировка: сначала самые новые материалы
    },
    take: limit,         // 4. Пагинация: размер пачки данных (take)
    skip: offset,        // 5. Пагинация: отступ от начала выборки (skip)
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-90", "title": "Архитектура потоков в Node", "content": ["Мидл", "Node.js", "Advanced"], "createdAt": "2026-06-10T11:00:00.000Z" },
      { "id": "post-40", "title": "Оптимизация Event Loop", "content": ["Node.js", "Мидл"], "createdAt": "2026-06-07T15:30:00.000Z" }
    ]
  */
}