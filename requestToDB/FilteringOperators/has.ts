// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовый поиск по массиву строк (Метод has)
// =========================================================================
// Логика: Оператор 'has' работает исключительно с полями-массивами (Scalar Lists, 
// например, String[]). Он проверяет, есть ли в этом массиве хотя бы один 
// конкретный указанный элемент. 
// КРИТИЧЕСКИЙ НЮАНС: В отличие от contains, метод 'has' требует стопроцентного 
// точечного совпадения элемента и НЕ поддерживает режим 'mode: insensitive'.

// Пример вызова: const posts = await findPostsByTag("NestJS")
export async function findPostsByTag(targetTag: string) {
  const posts = await prisma.post.findMany({
    where: {
      content: {
        has: targetTag, // Ищет строки, у которых в массиве content есть точный элемент "NestJS"
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "post-uuid-111",
        "title": "Изучаем бэкенд",
        "content": ["NestJS", "Prisma", "Postgres"], // Элемент найден в массиве
        "published": true,
        "authorId": "user-uuid-123"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Поиск по массиву с логическим фильтром (where + has)
// =========================================================================
// Логика: Совмещаем проверку массива с обычными плоскими полями. 
// База данных выберет только те публикации, которые уже допущены к печати 
// (published: true) и содержат в списке контента/тегов метку "Призма".

// Пример вызова: const activePrismaPosts = await getPublishedPostsByTag("Prisma")
export async function getPublishedPostsByTag(tag: string) {
  const posts = await prisma.post.findMany({
    where: {
      published: true, // Статичное условие
      content: {
        has: tag,      // Проверка наличия элемента в массиве
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "post-uuid-222",
        "title": "Шпаргалка по ORM",
        "content": ["Prisma", "TypeScript"],
        "published": true,
        "authorId": "user-uuid-123"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Жесткая проверка по автору и элементу массива (authorId + has)
// =========================================================================
// Логика: Ищем внутри таблицы постов записи конкретного разработчика, 
// у которых в массиве тегов или контента присутствует строго определенная технология.

// Пример вызова: const magoPosts = await getAuthorPostsByTag("user-uuid-123", "Postgres")
export async function getAuthorPostsByTag(userId: string, targetTech: string) {
  const posts = await prisma.post.findMany({
    where: {
      authorId: userId, // Фильтр по автору
      content: {
        has: targetTech, // Фильтр по массиву
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "post-uuid-111",
        "title": "Изучаем бэкенд",
        "content": ["NestJS", "Prisma", "Postgres"],
        "published": true,
        "authorId": "user-uuid-123"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Поиск по альтернативным элементам массива (has + OR)
// =========================================================================
// Логика: Повышаем сложность. Что делать, если нам нужно найти посты, в массиве 
// которых есть ИЛИ элемент "React", ИЛИ элемент "Vue"? 
// Сам по себе 'has' принимает только одну строку. Для множественного выбора 
// мы комбинируем несколько блоков 'has' внутри массива 'OR'.

// Пример вызова: const frontendPosts = await getFrontendPosts()
export async function getFrontendPosts() {
  const posts = await prisma.post.findMany({
    where: {
      OR: [
        { content: { has: "React" } }, // Вариант 1
        { content: { has: "Vue" } },   // Вариант 2
      ],
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-1", "title": "SPA на React", "content": ["React", "Redux"] },
      { "id": "post-2", "title": "Лайт-код на Vue", "content": ["Vue", "Vite"] }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Реляционный поиск по массивам (Ищем родителей через 'has' в детях)
// =========================================================================
// Логика: Продвинутый уровень. Нам нужно выгрузить список Пользователей (User), 
// но критерием поиска выступает массив внутри их связанных Постов. 
// Мы просим базу: "Дай мне авторов, у которых есть хотя бы один пост (`some`), 
// в массиве `content` которого лежит точное слово 'Архитектура'".

// Пример вызова: const architects = await findUsersByPostTag("Архитектура")
export async function findUsersByPostTag(specialtyTag: string) {
  const users = await prisma.user.findMany({
    where: {
      posts: {
        some: {
          content: {
            has: specialtyTag, // Проверяем массив строк внутри связанной таблицы Post
          },
        },
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 6: Боевой эндпоинт фильтрации тегов (has + Сортировка + Пагинация)
// =========================================================================
// Логика: Максимальная сложность. Симулируем реальную страницу категории на проде.
// Юзер кликает по тегу (например, "Мидл"). Бэкенд вытаскивает только опубликованные 
// статьи с этим элементом массива, сортирует их от свежих к старым и выдает порцией.

// Пример вызова: const cleanPage = await getPostsByTagPage("Мидл", 10, 0)
export async function getPostsByTagPage(targetKeyword: string, limit: number, offset: number) {
  const posts = await prisma.post.findMany({
    where: {
      published: true, // 1. Фильтр: Только открытые посты
      content: {
        has: targetKeyword, // 2. Фильтр: Массив должен содержать ключевое слово
      },
    },
    orderBy: {
      createdAt: 'desc', // 3. Сортировка: сначала новые статьи
    },
    take: limit,         // 4. Пагинация: размер страницы
    skip: offset,        // 5. Пагинация: отступ страницы
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-50", "title": "Код-ревью для Middle", "content": ["Мидл", "Node.js"], "createdAt": "2026-06-10T12:00:00.000Z" },
      { "id": "post-32", "title": "Архитектура JWT", "content": ["Мидл", "Auth"], "createdAt": "2026-06-08T09:15:00.000Z" }
    ]
  */
}