// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовое точное совпадение (Explicit equals)
// =========================================================================
// Логика: Самый строгий вариант фильтрации. База данных ищет записи, у которых 
// значение поля на 100% совпадает с переданным. Конструкция { city: { equals: "Дубай" } } 
// работает абсолютно так же, как и сокращенная { city: "Дубай" }, но явный оператор 
// делает код более читаемым в сложных динамических фильтрах.
// В SQL это превращается в команду: WHERE city = 'Дубай'

// Пример вызова: const users = await getUsersByExactCity("Дубай")
export async function getUsersByExactCity(cityName: string) {
  const users = await prisma.user.findMany({
    where: {
      city: {
        equals: cityName, // Найдет только "Дубай", строки "дубай" или "Дубай " (с пробелом) пропустит
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Точное совпадение без учета регистра (equals + mode: 'insensitive')
// =========================================================================
// Логика: В отличие от contains, мы требуем, чтобы слово совпадало полностью 
// от первой до последней буквы, но игнорируем регистр (большие или маленькие буквы).
// Полезно при авторизации или поиске по уникальным никнеймам.
// В SQL это превращается в команду: WHERE name ИЛIКЕ 'магомед'

// Пример вызова: const users = await getUserByNameCaseInsensitive("МАГОМЕД")
export async function getUserByNameCaseInsensitive(targetName: string) {
  const users = await prisma.user.findMany({
    where: {
      name: {
        equals: targetName,
        mode: 'insensitive', // Успешно найдет "Магомед", даже если в запросе пришло "МАГОМЕД"
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Точное совпадение по числам и Boolean (equals)
// =========================================================================
// Логика: Оператор `equals` незаменим, когда мы работаем с типами Int, Float 
// или Boolean, где частичный поиск (типа contains) физически невозможен.
// База данных мгновенно находит строки по четкому числовому критерию.

// Пример вызова: const users = await getUnblockedUsersByAge(25)
export async function getUnblockedUsersByAge(targetAge: number) {
  const users = await prisma.user.findMany({
    where: {
      isBlocked: { equals: false }, // Четкий фильтр по булеву флагу
      age: { equals: targetAge },    // Четкий фильтр по числу
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "age": 25,
        "isBlocked": false
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Комбинация точных совпадений через логическое ИЛИ (equals + OR)
// =========================================================================
// Логика: Повышаем сложность. Нам нужно выгрузить пользователей, которые 
// соответствуют строго одному из указанных точных параметров. 
// Заворачиваем блоки `equals` в массив `OR`.

// Пример вызова: const users = await StaffSelection()
export async function getStaffSelection() {
  const users = await prisma.user.findMany({
    where: {
      OR: [
        { role: { equals: "ADMIN" } }, // Либо строгий админ
        { city: { equals: "Дубай" } }, // Либо строго житель Дубая
      ],
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "user-1", "name": "Магомед", "city": "Дубай", "role": "USER" },
      { "id": "user-2", "name": "Алексей", "city": "Москва", "role": "ADMIN" }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Реляционный точный поиск через связи (equals + some)
// =========================================================================
// Логика: Продвинутый уровень. Мы ищем авторов, проверяя связанные данные.
// Нам нужны только те пользователи, у которых есть ХОТЯ БЫ ОДИН пост (`some`),
// название которого в точности равно строке "Привет, мир!".

// Пример вызова: const authors = await findAuthorsByExactPostTitle("Привет, мир!")
export async function findAuthorsByExactPostTitle(exactTitle: string) {
  const users = await prisma.user.findMany({
    where: {
      posts: {
        some: {
          title: {
            equals: exactTitle, // Ищем стопроцентное совпадение заголовка в таблице Post
          },
        },
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 6: Симуляция боевого эндпоинта (equals + Сортировка + Пагинация)
// =========================================================================
// Логика: Максимальная сложность. Полноценный продакшен-запрос для личного кабинета.
// Мы вытягиваем посты конкретного автора (точное совпадение authorId), которые 
// гарантированно опубликованы (published строго равен true). 
// Данные отдаем порциями с сортировкой от новых к старым.

// Пример вызова: const targetPage = await getUserPublishedPosts("user-uuid-123", 5, 0)
export async function getUserPublishedPosts(userId: string, limit: number, offset: number) {
  const posts = await prisma.post.findMany({
    where: {
      authorId: { equals: userId },  // 1. Фильтр: Только посты этого автора
      published: { equals: true },   // 2. Фильтр: Только опубликованные
    },
    orderBy: {
      createdAt: 'desc',             // 3. Сортировка: сначала свежие
    },
    take: limit,                     // 4. Пагинация: сколько взять строк
    skip: offset,                    // 5. Пагинация: сколько пропустить строк
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-50", "title": "Чистый код", "published": true, "authorId": "user-uuid-123", "createdAt": "2026-06-10T10:00:00.000Z" },
      { "id": "post-48", "title": "Паттерны в NestJS", "published": true, "authorId": "user-uuid-123", "createdAt": "2026-06-08T14:20:00.000Z" }
    ]
  */
}