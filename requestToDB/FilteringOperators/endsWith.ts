// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовый поиск по суффиксу (Регистрозависимый endsWith)
// =========================================================================
// Логика: Самый простой вариант. Проверяет, что строка заканчивается строго 
// на указанные символы. По умолчанию поиск чувствителен к регистру 
// (заглавные и строчные буквы для базы данных — это разные символы).
// В SQL это превращается в команду: WHERE title LIKE '%!'

// Пример вызова: const posts = await findPostsWithExclamation("!")
export async function findPostsWithExclamation(suffix: string) {
  const posts = await prisma.post.findMany({
    where: {
      title: {
        endsWith: suffix, // Напримеер, найдет "Привет, мир!", но пропустит "Привет, мир"
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "post-uuid-111",
        "title": "Привет, мир!",
        "published": true,
        "authorId": "user-uuid-123"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Поиск по концу строки без учета регистра (mode: 'insensitive')
// =========================================================================
// Логика: Идеальный сценарий для фильтрации пользователей по почтовым доменам.
// Флаг 'insensitive' отключает чувствительность к регистру.
// В SQL это превращается в команду: WHERE email ILIKE '%@dev.com'

// Пример вызова: const users = await getUsersByEmailDomain("@dev.com")
export async function getUsersByEmailDomain(domain: string) {
  const users = await prisma.user.findMany({
    where: {
      email: {
        endsWith: domain,
        mode: 'insensitive', // Найдет: mago@dev.com, MAGO@DEV.COM, test@Dev.com
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Поиск по суффиксу с жесткой фильтрацией (where + endsWith)
// =========================================================================
// Логика: Совмещаем поиск по хвосту строки со статичными условиями. 
// База сначала отсечет заблокированных юзеров и жителей других регионов, 
// а затем найдет тех, чей email заканчивается на определенный корпоративный домен.

// Пример вызова: const dubaiDevs = await findActiveDubaiUsersByDomain("@dev.com")
export async function findActiveDubaiUsersByDomain(domainSuffix: string) {
  const users = await prisma.user.findMany({
    where: {
      city: "Дубай",        // Жесткое условие 1
      isBlocked: false,    // Жесткое условие 2
      email: {
        endsWith: domainSuffix,
        mode: 'insensitive', // Проверяем хвост email
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай",
        "isBlocked": false
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Поиск окончания по нескольким полям сразу (endsWith + OR)
// =========================================================================
// Логика: Повышаем сложность. Ищем записи, у которых либо имя, либо email 
// заканчиваются на определенные символы (например, менеджер пытается вспомнить 
// пользователя, зная только концовку его данных).

// Пример вызова: const accounts = await searchAccountsBySuffix("med")
export async function searchAccountsBySuffix(query: string) {
  const users = await prisma.user.findMany({
    where: {
      OR: [
        { name: { endsWith: query, mode: 'insensitive' } },  // Найдена строка "Магомед"
        { email: { endsWith: query, mode: 'insensitive' } }, // Или почта вроде "ahmed@dev.com"
      ],
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "user-1", "email": "magomed@dev.com", "name": "Магомед" }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Реляционный поиск через связи (Ищем родителя по суффиксу в детях)
// =========================================================================
// Логика: Продвинутый уровень. Нам нужно получить список Авторов (User), 
// но фильтр мы накладываем на их публикации. 
// Оператор `some` выберет авторов, у которых хотя бы один пост заканчивается 
// на ключевое слово (например, "бэкенда").

// Пример вызова: const authors = await findAuthorsByPostSuffix("бэкенда")
export async function findAuthorsByPostSuffix(titleSuffix: string) {
  const users = await prisma.user.findMany({
    where: {
      posts: {
        some: {
          title: {
            endsWith: titleSuffix,
            mode: 'insensitive', // Проваливаемся в посты и чекаем конец заголовка
          },
        },
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 6: Боевой эндпоинт фильтрации архивных логов (endsWith + Пагинация)
// =========================================================================
// Логика: Максимальная сложность. Симулируем реальную задачу: выгребаем 
// порциями посты, заголовки которых заканчиваются на спец-метку (например, "[draft]"),
// строго сортируя их по дате создания от новых к старым.

// Пример вызова: const draftsPage = await getPostsBySuffixLabel("[draft]", 10, 0)
export async function getPostsBySuffixLabel(label: string, limit: number, offset: number) {
  const posts = await prisma.post.findMany({
    where: {
      title: {
        endsWith: label,
        mode: 'insensitive', // 1. Ищем метку в конце заголовка
      },
    },
    orderBy: {
      createdAt: 'desc',     // 2. Сортируем: сначала самые свежие
    },
    take: limit,             // 3. Лимитируем пачку (размер страницы)
    skip: offset,            // 4. Пропускаем строки для сдвига страниц
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-99", "title": "Архитектура мессенджера [Draft]", "createdAt": "2026-06-10T14:00:00.000Z" },
      { "id": "post-72", "title": "Базовый CRUD в Prisma [draft]", "createdAt": "2026-06-09T11:20:00.000Z" }
    ]
  */
}