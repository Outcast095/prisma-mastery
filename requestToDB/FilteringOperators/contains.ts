// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовый поиск подстроки (Регистрозависимый contains)
// =========================================================================
// Логика: Самый простой вариант. Ищет точное вхождение указанных символов 
// в любой части текста. По умолчанию поиск чувствителен к регистру 
// (буквы "A" и "a" для базы данных являются абсолютно разными).

// Пример вызова: const posts = await searchPostsByTitle("Архитектура")
export async function searchPostsByTitle(searchText: string) {
  const posts = await prisma.post.findMany({
    where: {
      title: {
        contains: searchText, // Найдет "Архитектура бэкенда", но пропустит "архитектура бэкенда"
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "post-uuid-111",
        "title": "Архитектура бэкенда",
        "published": true,
        "authorId": "user-uuid-123"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Поиск без учета регистра (contains + mode: 'insensitive')
// =========================================================================
// Логика: Главный стандарт для реальных поисковых строк на сайтах.
// Мы добавляем флаг `mode: 'insensitive'`. Теперь базе данных PostgreSQL 
// всё равно, какими буквами написан запрос — заглавными или строчными.

// Пример вызова: const users = await searchUsersByEmail("MaGo")
export async function searchUsersByEmail(searchEmail: string) {
  const users = await prisma.user.findMany({
    where: {
      email: {
        contains: searchEmail,
        mode: 'insensitive', // Найдет: mago@dev.com, MAGO@dev.com, Mago@dev.com
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Поиск с предварительной фильтрацией (where + contains)
// =========================================================================
// ...
// Логика: Мы совмещаем текстовый поиск по части слова с жесткими условиями.
// База сначала отсечет заблокированных пользователей и жителей других городов,
// а уже среди оставшихся найдет тех, чье имя содержит поисковый текст.

// Пример вызова: const dubaiUsers = await searchActiveUsersInDubai("мед")
export async function searchActiveUsersInDubai(nameQuery: string) {
  const users = await prisma.user.findMany({
    where: {
      city: "Дубай",       // Жесткое условие 1
      isBlocked: false,   // Жесткое условие 2
      name: {
        contains: nameQuery,
        mode: 'insensitive', // Поиск по подстроке ("мед" -> Найдется "Магомед")
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай",
        "isBlocked": false
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Поиск по нескольким полям сразу (contains + логическое OR)
// =========================================================================
// Логика: Повышаем сложность. Когда менеджер в админке вводит текст, он хочет,
// чтобы поиск срабатывал, если совпадение нашлось ИЛИ в имени, ИЛИ в email.
// Мы заворачиваем два оператора `contains` внутрь общего массива `OR`.

// Пример вызова: const accounts = await globalUserSearch("dev")
export async function globalUserSearch(query: string) {
  const users = await prisma.user.findMany({
    where: {
      OR: [
        { name: { contains: query, mode: 'insensitive' } },
        { email: { contains: query, mode: 'insensitive' } },
      ],
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    В ответ попадут и те, у кого "dev" есть в почте, и те, у кого в имени:

    [
      { "id": "user-1", "email": "magomed@dev.com", "name": "Магомед" },
      { "id": "user-2", "email": "alex@gmail.com", "name": "Dev-Developer" }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Реляционный поиск через связи (Поиск родителя по тексту в детях)
// =========================================================================
// Логика: Продвинутый уровень. Нам нужно найти Авторов (User), но поисковый 
// запрос мы проверяем по заголовкам их Публикаций (Post). 
// Оператор `some` говорит базе: "Выбери мне юзеров, у которых есть ХОТЯ БЫ один 
// пост, заголовок которого содержит указанный текст".

// Пример вызова: const authors = await findUsersByPostTitle("NestJS")
export async function findUsersByPostTitle(postQuery: string) {
  const users = await prisma.user.findMany({
    where: {
      posts: {
        some: {
          title: {
            contains: postQuery,
            mode: 'insensitive', // Проваливаемся в таблицу постов и ищем там текст
          },
        },
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Возвращает массив пользователей, которые написали посты со словом "NestJS" в названии:

    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 6: Симуляция боевого эндпоинта (contains + Пагинация + Сортировка)
// =========================================================================
// Логика: Максимальная сложность. Реальное API поиска на проде. Текст ищется 
// без учета регистра, но база дополнительно делает отступ (`skip`) для страниц, 
// ограничивает размер пачки (`take`) и сортирует результаты по дате.

// Пример вызова: const pageTwo = await getSearchPage("prisma", 10, 20)
export async function getSearchPage(search: string, limit: number, offset: number) {
  const results = await prisma.post.findMany({
    where: {
      title: {
        contains: search,
        mode: 'insensitive', // 1. Ищем текст
      },
    },
    orderBy: {
      createdAt: 'desc',     // 2. Сортируем: сначала новые
    },
    take: limit,             // 3. Ограничиваем количество (например, 10 штук)
    skip: offset,            // 4. Пропускаем первые строки (для переключения страниц)
  });

  return results;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-99", "title": "Prisma в 2026 году", "createdAt": "2026-06-10T12:00:00.000Z" },
      { "id": "post-98", "title": "Шпаргалка по Prisma", "createdAt": "2026-06-09T15:30:00.000Z" }
    ]
  */
}