// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовый поиск по префиксу (Регистрозависимый startsWith)
// =========================================================================
// Логика: Самый простой вариант. Проверяет, что строка начинается строго 
// с указанных символов. По умолчанию поиск чувствителен к регистру 
// (заглавные и строчные буквы для базы данных — это разные символы).
// В SQL это превращается в команду: WHERE name LIKE 'Маг%'

// Пример вызова: const posts = await findUsersByPrefix("Маг")
export async function findUsersByPrefix(prefix: string) {
  const users = await prisma.user.findMany({
    where: {
      name: {
        startsWith: prefix, // Найдет "Магомед", но пропустит "магомед"
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Поиск по началу строки без учета регистра (mode: 'insensitive')
// =========================================================================
// Логика: Полезно для поиска пользователей по первой части их email. 
// Флаг 'insensitive' отключает чувствительность к регистру, делая поиск гибким.
// В SQL это превращается в команду: WHERE email ILIKE 'mago%'

// Пример вызова: const users = await searchUsersByEmailStart("MaGo")
export async function searchUsersByEmailStart(emailPrefix: string) {
  const users = await prisma.user.findMany({
    where: {
      email: {
        startsWith: emailPrefix,
        mode: 'insensitive', // Найдет: mago@dev.com, MAGO@dev.com, Mago@dev.com
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Поиск по префиксу с жесткой фильтрацией (where + startsWith)
// =========================================================================
// Логика: Совмещаем поиск по началу строки с другими условиями. 
// База данных сначала отберет только активных жителей Дубая, а уже среди них 
// отфильтрует тех, чье имя начинается на заданную букву.

// Пример вызова: const dubaiUsers = await findActiveDubaiUsersByLetter("М")
export async function findActiveDubaiUsersByLetter(letter: string) {
  const users = await prisma.user.findMany({
    where: {
      city: "Дубай",        // Жесткое условие 1
      isBlocked: false,    // Жесткое условие 2
      name: {
        startsWith: letter,
        mode: 'insensitive', // Ищем имя, начинающееся на "М" или "м"
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай",
        "isBlocked": false
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Поиск по нескольким полям на совпадение старта (startsWith + OR)
// =========================================================================
// Логика: Повышаем сложность. Сценарий, когда на фронтенде есть одно поле поиска, 
// и мы хотим найти пользователя, если введенный текст совпадает либо с началом 
// его имени, либо с началом его email (например, поиск по юзернейму).

// Пример вызова: const accounts = await searchAccountsByPrefix("mago")
export async function searchAccountsByPrefix(query: string) {
  const users = await prisma.user.findMany({
    where: {
      OR: [
        { name: { startsWith: query, mode: 'insensitive' } },
        { email: { startsWith: query, mode: 'insensitive' } },
      ],
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "user-1", "email": "magomed@dev.com", "name": "Магомед" },
      { "id": "user-2", "email": "magon-tech@gmail.com", "name": "Инновации" }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Реляционный поиск через связи (Ищем родителя по префиксу в детях)
// =========================================================================
// Логика: Продвинутый уровень. Нам нужно найти Авторов (User), но фильтр 
// мы накладываем на их Публикации (Post). 
// Оператор `some` выберет тех пользователей, у которых есть ХОТЯ БЫ один пост, 
// заголовок которого начинается строго с определенного слова (например, "NestJS").

// Пример вызова: const authors = await findAuthorsByPostPrefix("NestJS")
export async function findAuthorsByPostPrefix(titlePrefix: string) {
  const users = await prisma.user.findMany({
    where: {
      posts: {
        some: {
          title: {
            startsWith: titlePrefix,
            mode: 'insensitive', // Проваливаемся в таблицу постов и проверяем префикс названия
          },
        },
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Возвращает авторов, которые написали статьи, начинающиеся на слово "NestJS":

    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 6: Боевой эндпоинт автодополнения (startsWith + take + orderBy)
// =========================================================================
// Логика: Максимальная сложность. Идеально для механики автокомплита 
// (Autocomplete / Наводящие подсказки при вводе). 
// Когда юзер вбивает первые буквы в поисковую строку, база мгновенно находит 
// первые 5 свежих совпадений, отсортированных по дате их создания.

// Пример вызова: const suggestions = await getSearchSuggestions("прив")
export async function getSearchSuggestions(currentInput: string) {
  const suggestions = await prisma.post.findMany({
    where: {
      title: {
        startsWith: currentInput,
        mode: 'insensitive', // 1. Ищем совпадение по началу строки
      },
    },
    orderBy: {
      createdAt: 'desc',     // 2. Сортируем: сначала самые свежие публикации
    },
    take: 5,                 // 3. Жестко лимитируем выдачу (только первые 5 подсказок)
    select: {
      id: true,
      title: true,           // 4. Облегчаем трафик: берем только ID и заголовок
    }
  });

  return suggestions;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-99", "title": "Привет, мир! Изучаем NestJS" },
      { "id": "post-45", "title": "Приветственный гайд по базам данных" }
    ]
  */
}