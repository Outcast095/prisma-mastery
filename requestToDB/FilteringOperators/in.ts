// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовый поиск по списку строк (Фильтр по городам)
// =========================================================================
// Логика: Оператор 'in' принимает массив значений и находит записи, у которых 
// значение поля совпадает хотя бы с одним элементом из этого списка. 
// Идеально заменяет длинные цепочки условий OR.
// В SQL это превращается в команду: WHERE city IN ('Дубай', 'Москва')

// Пример вызова: const users = await getUsersByCities(['Дубай', 'Москва'])
export async function getUsersByCities(targetCities: string[]) {
  const users = await prisma.user.findMany({
    where: {
      city: {
        in: targetCities, // Ищет строки, где город равен либо "Дубай", либо "Москва"
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "user-1", "name": "Магомед", "city": "Дубай" },
      { "id": "user-2", "name": "Алиса", "city": "Москва" }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Фильтрация по списку системных ENUM-значений (Фильтр по ролям)
// =========================================================================
// Логика: Часто используется в блоках авторизации и админках, когда нужно выбрать 
// пользователей, обладающих определенным уровнем доступа (например, только стафф).

// Пример вызова: const staff = await getStaffUsers()
export async function getStaffUsers() {
  const staff = await prisma.user.findMany({
    where: {
      role: {
        in: ['ADMIN', 'USER'], // Выбирает пользователей, чья роль входит в этот массив
      },
    },
  });

  return staff;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "user-uuid-111", "email": "admin@dev.com", "role": "ADMIN" },
      { "id": "user-uuid-222", "email": "mod@dev.com", "role": "MODERATOR" }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Поиск по списку чисел с предварительной фильтрацией (where + in)
// =========================================================================
// Логика: Совмещаем списочный фильтр 'in' по числовому полю со статичными условиями.
// База сначала отсечет заблокированных, а затем найдет тех, чей возраст 
// равен строго одному из «юбилейных» чисел.

// Пример вызова: const milestoneUsers = await getActiveUsersByAges([18, 25, 30])
export async function getActiveUsersByAges(targetAges: number[]) {
  const users = await prisma.user.findMany({
    where: {
      isBlocked: false, // Жесткое условие 1
      age: {
        in: targetAges, // Поле age должно быть строго 18, 25 или 30
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "user-1", "name": "Магомед", "age": 25, "isBlocked": false },
      { "id": "user-3", "name": "Иван", "age": 18, "isBlocked": false }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Выборка постов по динамическому списку ID авторов (ID в массиве)
// =========================================================================
// Логика: Повышаем сложность. Сценарий из реальной ленты новостей. Фронтенд присылает 
// массив ID подписок пользователя, и нам нужно выгребсти все публикации, 
// созданные авторами из этого списка.

// Пример вызова: const feed = await getFeedByAuthors(['uuid-mago', 'uuid-alex'])
export async function getFeedByAuthors(followingIds: string[]) {
  const posts = await prisma.post.findMany({
    where: {
      authorId: {
        in: followingIds, // Находит посты, у которых внешнее поле authorId есть в массиве подписок
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-1", "title": "Prisma Guide", "authorId": "uuid-mago" },
      { "id": "post-2", "title": "NestJS Architecture", "authorId": "uuid-alex" }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Реляционный списочный поиск через связи (in + some)
// =========================================================================
// Логика: Продвинутый уровень. Нам нужно найти Пользователей (User), проверяя их 
// дочерние записи. Мы просим базу: "Найди мне авторов, у которых есть хотя бы 
// один пост (`some`), чей идентификатор (`id`) входит в наш целевой список".

// Пример вызова: const authors = await findAuthorsBySpecificPosts(['post-100', 'post-200'])
export async function findAuthorsBySpecificPosts(targetPostIds: string[]) {
  const users = await prisma.user.findMany({
    where: {
      posts: {
        some: {
          id: {
            in: targetPostIds, // Проваливаемся в таблицу постов и проверяем их ID по списку
          },
        },
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 6: Боевой эндпоинт таргетированной выгрузки (in + Сортировка + Пагинация)
// =========================================================================
// Логика: Максимальная сложность. Симулируем реальную бизнес-задачу: вытягиваем 
// активных пользователей из целевых регионов (переданных массивом), 
// сортируем их по дате регистрации и отдаем строго порциями для постраничного вывода.

// Пример вызова: const targetPage = await getMarketingUsersPage(['Дубай', 'Абу-Даби'], 10, 0)
export async function getMarketingUsersPage(targetCities: string[], limit: number, offset: number) {
  const users = await prisma.user.findMany({
    where: {
      city: {
        in: targetCities,    // 1. Фильтр: Только пользователи из целевых городов
      },
      isBlocked: false,      // 2. Фильтр: Только активные аккаунты
    },
    orderBy: {
      createdAt: 'desc',     // 3. Сортировка: сначала самые новые
    },
    take: limit,             // 4. Пагинация: размер страницы (take)
    skip: offset,            // 5. Пагинация: сдвиг (skip)
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "user-99", "name": "Ахмед", "city": "Дубай", "createdAt": "2026-06-10T12:00:00.000Z" },
      { "id": "user-84", "name": "Саид", "city": "Абу-Даби", "createdAt": "2026-06-09T16:45:00.000Z" }
    ]
  */
}