// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовое исключение по списку строк (Фильтр нежелательных городов)
// =========================================================================
// Логика: Оператор 'notIn' — это полная противоположность оператора 'in'. Он принимает 
// массив значений и выбрасывает из выдачи все записи, у которых поле совпало 
// хотя бы с одним элементом из этого списка. 
// В SQL это превращается в команду: WHERE city NOT IN ('Москва', 'Питер')

// Пример вызова: const internationalUsers = await getUsersOutsideRU(['Москва', 'Питер'])
export async function getUsersOutsideRU(excludedCities: string[]) {
  const users = await prisma.user.findMany({
    where: {
      city: {
        notIn: excludedCities, // Нам подойдут любые города (например, Дубай), кроме указанных
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай" // Успешно прошел фильтр исключения
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Исключение системных ENUM-значений (Выборка только обычных клиентов)
// =========================================================================
// Логика: Используется, когда нужно получить чистый список клиентов для рассылки, 
// полностью убрав из выдачи любой персонал (администраторов, модераторов, техподдержку).

// Пример вызова: const regularClients = await getOnlyClients()
export async function getOnlyClients() {
  const clients = await prisma.user.findMany({
    where: {
      role: {
        notIn: ['ADMIN', 'ADMIN'], // Исключаем стафф-роли из итогового ответа
      },
    },
  });

  return clients;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "user-1", "email": "client1@dev.com", "role": "USER" },
      { "id": "user-2", "email": "client2@dev.com", "role": "USER" }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Исключение списка чисел с предварительной фильтрацией (where + notIn)
// =========================================================================
// Логика: Совмещаем жесткие условия проверки с исключением числовых диапазонов. 
// Выбираем активных пользователей из Дубая, исключая определенные «тестовые» 
// или пограничные возраста.

// Пример вызова: const filteredDubaiUsers = await getDubaiUsersExceptAges([18, 21])
export async function getDubaiUsersExceptAges(blacklistedAges: number[]) {
  const users = await prisma.user.findMany({
    where: {
      city: "Дубай",       // Жесткое условие 1
      isBlocked: false,   // Жесткое условие 2
      age: {
        notIn: blacklistedAges, // Возраст не должен быть равен 18 или 21
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "age": 25, // Прошел, так как 25 нет в списке [18, 21]
        "city": "Дубай",
        "isBlocked": false
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Исключение постов от забаненных авторов (Внешний ключ + notIn)
// =========================================================================
// Логика: Повышаем сложность. У нас есть массив ID злостных нарушителей или 
// ботов. Нам нужно отфильтровать общую ленту постов так, чтобы публикации 
// этих авторов вообще не попали на экран.

// Пример вызова: const cleanFeed = await getCleanFeed(['bad-user-id-1', 'bad-user-id-2'])
export async function getCleanFeed(bannedAuthorIds: string[]) {
  const posts = await prisma.post.findMany({
    where: {
      authorId: {
        notIn: bannedAuthorIds, // Поле связи authorId не должно содержать ID из черного списка
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "post-1", "title": "Prisma Tips", "authorId": "user-uuid-123" }, // Автор Магомед (не забанен)
      { "id": "post-2", "title": "NestJS Architecture", "authorId": "user-uuid-555" }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Реляционное исключение через связи (notIn + some)
// =========================================================================
// Логика: Продвинутый уровень. Мы ищем Пользователей, но фильтруем их по содержимому 
// таблицы Постов. Мы просим базу: "Найди мне авторов, у которых есть хотя бы один 
// пост (`some`), чей идентификатор (`id`) НЕ входит в список заархивированных или скрытых ID".

// Пример вызова: const activeAuthors = await findAuthorsWithVisiblePosts(['post-hidden-1', 'post-hidden-2'])
export async function findAuthorsWithVisiblePosts(hiddenPostIds: string[]) {
  const users = await prisma.user.findMany({
    where: {
      posts: {
        some: {
          id: {
            notIn: hiddenPostIds, // Проверяем, что ID постов автора находятся вне списка скрытых постов
          },
        },
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "city": "Дубай"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 6: Боевой эндпоинт фильтрации лидов (notIn + Сортировка + Пагинация)
// =========================================================================
// Логика: Максимальная сложность. Симулируем реальную выгрузку в CRM-систему. 
// Мы берем активных пользователей, полностью исключая из выборки внутренние 
// тестовые доменные имена (переданные списком id или городов), сортируем их по 
// дате регистрации и отдаем порциями.

// Пример вызова: const crmPage = await getCrmUsersWithoutTestCities(['Тестовый Город', 'Локахост Сити'], 10, 0)
export async function getCrmUsersWithoutTestCities(testCities: string[], limit: number, offset: number) {
  const users = await prisma.user.findMany({
    where: {
      city: {
        notIn: testCities,   // 1. Фильтр: Убираем мусорные/тестовые локации из аналитики
      },
      isBlocked: false,      // 2. Фильтр: Только активные аккаунты
    },
    orderBy: {
      createdAt: 'desc',     // 3. Сортировка: сначала самые новые лиды
    },
    take: limit,             // 4. Пагинация: сколько записей вернуть
    skip: offset,            // 5. Пагинация: сколько записей пропустить
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    [
      { "id": "user-200", "name": "Магомед", "city": "Дубай", "createdAt": "2026-06-10T14:30:00.000Z" },
      { "id": "user-199", "name": "Алексей", "city": "Москва", "createdAt": "2026-06-10T11:15:00.000Z" }
    ]
  */
}