import { prisma } from "../src/prisma.js";




// =========================================================================
// ПРИМЕР 1: Базовая Сортировка и Лимит (orderBy + take)
// =========================================================================
// Логика: Нам нужно показать на главной странице виджет «3 самых свежих поста».
// Мы сортируем посты по дате создания от новых к старым (desc) и берем ровно 3 штуки.

// Пример вызова: const topPosts = await getNewestPostsPreview()
async function getNewestPostsPreview() {
  const posts = await prisma.post.findMany({
    where: { published: true },
    orderBy: {
      createdAt: "desc", // "desc" — по убыванию (новые сверху), "asc" — по возрастанию
    },
    take: 3, // База данных вернет строго максимум 3 записи
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернется массив из 3 постов, отсортированных строго по времени создания 
    (от июня к маю 2026 года):

    [
      {
        "id": "post-uuid-3",
        "title": "Третий пост",
        "createdAt": "2026-06-09T10:00:00.000Z"
      },
      {
        "id": "post-uuid-2",
        "title": "Второй пост",
        "createdAt": "2026-06-08T14:15:00.000Z"
      },
      {
        "id": "post-uuid-1",
        "title": "Самый первый пост",
        "createdAt": "2026-05-20T09:00:00.000Z"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Сортировка по Связанным Полям (Продвинутый orderBy)
// =========================================================================
// Логика: Prisma умеет сортировать записи по полям из других таблиц! 
// Задача: Вывести список постов, но отсортировать их по алфавиту... на основе ИМЕНИ АВТОРА.

// Пример вызова: const sortedByAuthor = await getPostsSortedByAuthorName()
async function getPostsSortedByAuthorName() {
  const posts = await prisma.post.findMany({
    orderBy: {
      author: {
        name: "asc", // Сортируем посты по имени юзера от А до Я
      },
    },
    select: {
      title: true,
      author: { select: { name: true } },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Сначала пойдут посты, написанные Антоном, затем Магомедом, затем Яной:

    [
      { "title": "Пост Антона", "author": { "name": "Антон" } },
      { "title": "Пост Магомеда", "author": { "name": "Магомед" } },
      { "title": "Пост Яны", "author": { "name": "Яна" } }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Пагинация на основе Смещения (Offset Pagination: take + skip)
// =========================================================================
// Логика: Классическая пагинация (Страница 1, 2, 3...). Фронтенд присылает нам 
// номер страницы (page) и сколько элементов нужно на одной странице (limit).
// ФОРМУЛА для skip: (page - 1) * limit



// Пример вызова: const pageTwo = await getPostsByOffsetPage(2, 10) -> Страница 2, по 10 штук на страницу
async function getPostsByOffsetPage(page: number, limit: number) {
  const postsSkip = (page - 1) * limit; // Для page=2 и limit=10: (2-1)*10 = skip: 10

  const posts = await prisma.post.findMany({
    orderBy: { createdAt: "desc" },
    skip: postsSkip, // Пропускаем первые 10 постов (которые ушли на 1-ю страницу)
    take: limit,     // Забираем следующие 10 постов (для 2-й страницы)
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Если в базе всего 25 постов, то для 2-й страницы (skip: 10, take: 10) 
    мы пропустим посты с 1 по 10 и получим посты с 11 по 20:

    [
      { "id": "post-uuid-11", "title": "11-й по счету пост базы данных..." },
      ...
      { "id": "post-uuid-20", "title": "20-й по счету пост базы данных..." }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Курсорная Пагинация (Cursor Pagination — Продвинутая)
// =========================================================================
// Логика: Идеально для бесконечных лент (Infinite Scroll) типа соцсетей. 
// Вместо пропуска абстрактного числа записей (`skip`), мы говорим базе данных: 
// «Дай мне 5 постов, начиная СТРОГО ПОСЛЕ поста с ID таком-то».
// ПРАВИЛО: Ключ skip: 1 используется для того, чтобы сам пост-курсор не дублировался в ответе.

// Пример вызова: const nextPack = await getPostsByCursor("post-uuid-20", 5)
async function getPostsByCursor(lastPostId: string | null, limit: number) {
  const posts = await prisma.post.findMany({
    orderBy: { id: "asc" }, // Для курсорной пагинации нужна стабильная сортировка (обычно по id)
    take: limit,
    
    // Если это самый первый запрос (лента только открылась), cursor не передаем.
    // Если это дозагрузка, передаем ID последнего увиденного поста.
    ...(lastPostId && {
      cursor: { id: lastPostId },
      skip: 1, // Пропускаем сам пост lastPostId, забираем строго следующие за ним
    }),
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Передав "post-uuid-20" и limit: 2, мы получим посты 21 и 22:

    [
      { "id": "post-uuid-21", "title": "Пост двадцать первый" },
      { "id": "post-uuid-22", "title": "Пост двадцать второй" }
    ]
  */
}