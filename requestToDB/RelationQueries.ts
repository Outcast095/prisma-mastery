import { prisma } from "../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Метод select (Выборочное извлечение полей и экономия трафика)
// =========================================================================
// Логика: Заставляет базу данных отдавать ТОЛЬКО те поля, которые мы указали.
// КРИТИЧЕСКОЕ ПРАВИЛО: Нельзя использовать include и select на одном уровне!
// Если нужно ограничить поля в связи, мы вкладываем один select внутрь другого.

// Пример вызова: const feedData = await getEconomicalPostsFeed()
async function getEconomicalPostsFeed() {
  const posts = await prisma.post.findMany({
    select: {
      id: true,
      title: true, // Забираем только id и title из самого поста (content и даты игнорируются)
      author: {
        select: {
          name: true, // Из автора берем ТОЛЬКО имя (скрываем email, роуты, пароли)
        },
      },
    },
  });

  // Вернет массив облегченных объектов. Идеально для генерации фида новостей.
  return posts; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Обрати внимание: в объектах отсутствуют поля content, published, createdAt.
    В объекте автора осталось строго одно поле name. Вес такого ответа минимален:

    [
      {
        "id": "post-uuid-aaa",
        "title": "Мой первый пост про Prisma",
        
        // Появился благодаря вложенному select
        "author": {
          "name": "Магомед"
        }
      },
      {
        "id": "post-uuid-bbb",
        "title": "Изучаю базы данных",
        "author": {
          "name": "Магомед"
        }
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Оператор связи some (Хотя бы один связанный элемент)
// =========================================================================
// Логика: Фильтрует родительские записи (посты) на основе контента в связях (тегах).
// «Найди посты, у которых ХОТЯ БЫ ОДИН тег соответствует условию».

// Пример вызова: const postsWithTag = await findPostsByTagName("postgres")
async function findPostsByTagName(tagName: string) {
  const posts = await prisma.post.findMany({
    where: {
      tags: {
        some: {
          name: tagName, // Ищет посты, где среди тегов есть тег с именем "postgres"
        },
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Функция вернет массив стандартных объектов Post, которые привязаны к тегу "postgres".
    Сам массив тегов внутрь объектов по дефолту не подмешивается (для этого нужен include):

    [
      {
        "id": "post-uuid-aaa",
        "title": "Мой первый пост про Prisma",
        "content": ["Prisma — это круто!"],
        "published": true,
        "authorId": "user-uuid-123",
        "createdAt": "2026-06-09T13:00:00.000Z",
        "updatedAt": "2026-06-09T13:00:00.000Z"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Оператор связи none (Ни один из связанных элементов)
// =========================================================================
// Логика: Фильтрует родительские записи. «Найди посты, у которых НИ ОДИН тег 
// не соответствует условию». Используется для черных списков и исключения контента.

// Пример вызова: const cleanPosts = await findPostsWithoutBannedTags(["spam", "flood"])
async function findPostsWithoutBannedTags(bannedTagNames: string[]) {
  const posts = await prisma.post.findMany({
    where: {
      tags: {
        none: {
          name: {
            in: bannedTagNames, // Найдет посты, у которых вообще НЕТ тегов "spam" или "flood"
          },
        },
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    В массив попадут только те посты, у которых либо вообще нет тегов, 
    либо их теги не пересекаются со словами "spam" и "flood":

    [
      {
        "id": "post-uuid-bbb",
        "title": "Изучаю базы данных",
        "content": ["PostgreSQL отлично сочетается с Node.js."],
        "published": false,
        "authorId": "user-uuid-123",
        "createdAt": "2026-06-09T14:15:00.000Z",
        "updatedAt": "2026-06-09T14:15:00.000Z"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Оператор связи every (Абсолютно все связанные элементы)
// =========================================================================
// Логика: «Найди ЗАПИСИ, у которых ВСЕ связанные элементы соответствуют условию».
// Если у сущности найдется хоть один связанный объект, не прошедший фильтр — вся запись отбрасывается.
// КРИТИЧЕСКИЙ НЮАНС: Пользователи, у которых вообще нет постов (0 записей), тоже пройдут эту проверку!

// Пример вызова: const trustedUsers = await getUsersWithAllPremiumPosts()
async function getUsersWithAllPremiumPosts() {
  const users = await prisma.user.findMany({
    where: {
      posts: {
        every: {
          published: true, // Найдет только тех юзеров, у которых ВСЕ до единого посты опубликованы.
        },
      },
    },
  });

  // Если у юзера 10 постов и хотя бы 1 скрыт (published: false) — этот юзер НЕ вернется.
  return users; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    В выборку попадут пользователи, у которых нет ни одного черновика (published: false):

    [
      {
        "id": "user-uuid-999",
        "email": "alex@dev.com",
        "name": "Алекс",
        "age": 30,
        "isEmailVerified": true,
        "city": "Берлин",
        "isBlocked": false,
        "isPremium": false,
        "role": "USER",
        "status": "EMPLOYED",
        "createdAt": "2026-01-15T08:00:00.000Z",
        "updatedAt": "2026-05-20T12:00:00.000Z"
      }
    ]
  */
}