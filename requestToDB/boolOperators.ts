// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Оператор AND (Логическое "И")
// =========================================================================
// Логика: Этот оператор требует, чтобы все условия внутри массива были выполнены 
// одновременно. Если хотя бы одно условие ложно — запись отбрасывается.
// (В Prisma перечисление полей через запятую в where уже работает как неявный AND, 
// но явный блок AND незаменим при построении сложных вложенных комбинаций).

// Пример вызова: const users = await getVerifiedAdultUsers()
async function getVerifiedAdultUsers() {
  const users = await prisma.user.findMany({
    where: {
      // Оператор AND принимает массив объектов-условий
      AND: [
        { age: { gte: 18 } },         // Условие 1: Возраст больше или равен 18
        { isEmailVerified: true },    // Условие 2: Email подтвержден
      ],
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернется массив пользователей, которые ОДНОВРЕМЕННО и совершеннолетние, 
    и подтвердили свой почтовый ящик:

    [
      {
        "id": "user-uuid-111",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "age": 25,                 // Прошел gte: 18
        "isEmailVerified": true,   // Прошел точное совпадение
        "createdAt": "2026-06-09T12:00:00.000Z"
      },
      {
        "id": "user-uuid-222",
        "email": "aslan@dev.com",
        "name": "Аслан",
        "age": 18,                 // Прошел gte: 18
        "isEmailVerified": true,   // Прошел точное совпадение
        "createdAt": "2026-06-09T13:15:00.000Z"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Оператор OR (Логическое "ИЛИ")
// =========================================================================
// Логика: Этот оператор требует, чтобы выполнилось хотя бы одно из указанных условий. 
// Если пользователь подходит под первое условие, под второе или под оба сразу — 
// он гарантированно попадет в итоговый результат.

// Пример вызова: const users = await getPremiumOrStaffUsers()
async function getPremiumOrStaffUsers() {
  const users = await prisma.user.findMany({
    where: {
      // Оператор OR также принимает массив условий
      OR: [
        { role: 'ADMIN' },         // Условие 1: Пользователь является администратором
        { isPremium: true },       // Условие 2: ИЛИ у пользователя активен премиум-статус
      ],
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    В выборку попадут как обычные премиум-пользователи, так и администраторы 
    (даже если у админа нет премиума):

    [
      {
        "id": "user-uuid-333",
        "email": "premium-user@dev.com",
        "role": "USER",
        "isPremium": true          // Прошел по условию 2
      },
      {
        "id": "user-uuid-444",
        "email": "admin-boss@dev.com",
        "role": "ADMIN",           // Прошел по условию 1
        "isPremium": false 
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Оператор NOT (Логическое "НЕ")
// =========================================================================
// Логика: Полностью исключает из выборки тех, кто соответствует указанным условиям. 
// В отличие от AND/OR, оператор NOT в Prisma универсален: он может принимать 
// как один одиночный объект, так и массив объектов (для исключения по нескольким фронтам).

// Пример вызова: const users = await getActiveUsersWithoutBannedCountries("США")
async function getActiveUsersWithoutBannedCountries(bannedCountry: string) {
  const users = await prisma.user.findMany({
    where: {
      // Оператор NOT исключит всех user, кто попал хотя бы под одно его условие
      NOT: [
        { country: bannedCountry }, // Исключаем пользователей из забаненной страны
        { isPremium: true },        // Исключаем пользователей с премиум-статусом
      ],
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернутся пользователи, у которых страна НЕ равна переданной И статус premium равен false:

    [
      {
        "id": "user-uuid-555",
        "email": "dubai-dev@dev.com",
        "country": "ОАЭ",          // Успешно (не США)
        "isPremium": false         // Успешно (не премиум)
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Неявное объединение операторов (Комбинация OR и NOT)
// =========================================================================
// Логика: Когда ты располагаешь разные операторы на одном уровне объекта where, 
// Prisma автоматически связывает их между собой через скрытое "И" (AND).
// В данном случае: город должен быть (Москва ИЛИ Дубай) И при этом юзер НЕ заблокирован.

// Пример вызова: const users = await getTargetUsers()
async function getTargetUsers() {
  const users = await prisma.user.findMany({
    where: {
      // 1. Сначала ставим условие географии: город должен быть Москва ИЛИ Дубай
      OR: [
        { city: 'Москва' },
        { city: 'Дубай' }
      ],
      // 2. И на этом же уровне добавляем жесткое исключение заблокированных
      NOT: {
        isBlocked: true 
      }
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Юзеры из указанных городов, у которых флаг исBlocked строго равен false:

    [
      {
        "id": "user-uuid-666",
        "name": "Магомед",
        "city": "Дубай",           // Соответствует OR
        "isBlocked": false         // Соответствует NOT
      },
      {
        "id": "user-uuid-777",
        "name": "Дмитрий",
        "city": "Москва",          // Соответствует OR
        "isBlocked": false         // Соответствует NOT
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Глубокая вложенная фильтрация (AND + OR + NOT + contains)
// =========================================================================
// Логика: Оператор contains осуществляет гибкий поиск по части строки (подстроке) 
// в любом месте поля (в начале, середине или конце).
// Этот пример объединяет всё: имя должно содержать "Magomed" ИЛИ "Akhmed", 
// возраст должен быть строго больше 18 (gt: 18) И статус НЕ должен быть равен "EMPLOYED".

// Пример вызова: const candidates = await searchDevelopersForProject()
async function searchDevelopersForProject() {
  const candidates = await prisma.user.findMany({
    where: {
      // Главный блок AND объединяет жесткие условия, которые обязаны выполниться вместе
      AND: [
        // 1. Первое условие внутри AND — это блок OR (выбор по имени через contains)
        {
          OR: [ 
            { name: { contains: 'Magomed' } },
            { name: { contains: 'Akhmed' } }
          ]
        },
        // 2. Второе условие внутри AND — возраст строго больше 18 лет
        {
          age: { gt: 18 }
        },
        // 3. Третье условие внутри AND — отсечение по статусу работы (NOT)
        {
          NOT: {
            status: 'EMPLOYED' 
          }
        }
      ]
    }
  });

  return candidates;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернутся свободные совершеннолетние разработчики, подходящие по маске имени:

    [
      {
        "id": "user-uuid-888",
        "name": "Magomed Karasaev", // Прошел contains ('Magomed' наден в начале)
        "age": 25,                  // Прошел gt: 18
        "status": "UNEMPLOYED"      // Прошел NOT status: 'EMPLOYED'
      },
      {
        "id": "user-uuid-999",
        "name": "Akhmedov Ali",     // Прошел contains ('Akhmed' найден в составе фамилии)
        "age": 30,                  // Прошел gt: 18
        "status": "OPEN_TO_WORK"    // Прошел NOT status: 'EMPLOYED'
      }
    ]
  */
}