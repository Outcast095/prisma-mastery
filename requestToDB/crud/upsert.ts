// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовый метод upsert (Синхронизация профиля по Email)
// =========================================================================
// Логика: Атомарная операция "два в одном". Блок 'where' проверяет наличие записи по уникальному полю.
// Если запись НАЙДЕНА -> Prisma выполняет блок 'update' (обновляет существующую строку).
// Если запись НЕ НАЙДЕНА -> Prisma выполняет блок 'create' (создает новую запись с нуля).
// Используем только гарантированные поля схемы (email, city, age), чтобы избежать ошибок компиляции.

// Пример вызова: const user = await syncUserCity("mago@dev.com", "Дубай")
export async function syncUserCity(email: string, currentCity: string) {
  const user = await prisma.user.upsert({
    where: {
      email: email, // Ищем строго по уникальному полю email
    },
    update: {
      city: currentCity, // Если юзер есть, просто актуализируем его город
    },
    create: {
      email: email,
      city: currentCity, // Если юзера нет, создаем профиль с этим городом
      age: 25,           // Задаем базовый обязательный возраст для новой записи
      name: "Магомед",
    },
  });

  return user;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ (СЦЕНАРИЙ А: ЮЗЕР БЫЛ В БАЗЕ — ПРОИЗОШЕЛ UPDATE) (JSON):
    ========================================================================
    {
      "id": "user-uuid-777",
      "email": "mago@dev.com",
      "name": "Магомед",
      "age": 25,
      "city": "Дубай", 
      "role": "USER",
      "createdAt": "2026-05-15T10:00:00.000Z",
      "updatedAt": "2026-06-10T09:00:00.000Z" // Дата изменилась
    }

    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ (СЦЕНАРИЙ Б: ЮЗЕРА НЕ БЫЛО — ПРОИЗОШЕЛ CREATE) (JSON):
    ========================================================================
    {
      "id": "user-brand-new-uuid-111",
      "email": "mago@dev.com",
      "name": "Магомед",
      "age": 25,
      "city": "Дубай",
      "role": "USER",
      "createdAt": "2026-06-10T09:00:00.000Z",
      "updatedAt": "2026-06-10T09:00:00.000Z"
    }
  */
}


// =========================================================================
// ПРИМЕР 2: Атомарная математика в upsert (Увеличение счетчика/возраста)
// =========================================================================
// Логика: В блоке 'update' можно использовать специальные операторы вроде 'increment'.
// Сценарий: Если пользователь авторизуется повторно, мы увеличиваем его условный 
// «возраст/уровень» (или счетчик заходов), а если он зашел впервые — создаем запись с базовым числом.

// Пример вызова: const user = await handleUserLoginActivity("mago@dev.com")
export async function handleUserLoginActivity(email: string) {
  const user = await prisma.user.upsert({
    where: { email: email },
    update: {
      age: {
        increment: 1, // Если найден — атомарно прибавляем 1 к текущему возрасту в базе
      },
    },
    create: {
      email: email,
      age: 18,        // Если это первый визит — выставляем дефолтные 18 лет
      name: "Новый Пользователь",
    },
  });

  return user;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ (ПРИ ПОВТОРНОМ ЗАХОДЕ ЮЗЕРА С ВОЗРАСТОМ 25) (JSON):
    ========================================================================
    {
      "id": "user-uuid-777",
      "email": "mago@dev.com",
      "age": 26, // Стало 26 на уровне PostgreSQL без вычитки в Node.js
      "createdAt": "2026-05-15T10:00:00.000Z",
      "updatedAt": "2026-06-10T09:05:00.000Z"
    }
  */
}


// =========================================================================
// ПРИМЕР 3: Умный upsert + select (Безопасный ответ без системных данных)
// =========================================================================
// Логика: Как и обычные методы чтения, upsert поддерживает блок 'select'.
// Это гарантирует, что независимо от того, создалась строка или обновилась, 
// наружу уйдет только безопасная, строго отфильтрованная проекция полей.

// Пример вызова: const publicData = await syncAndGetPublicProfile("mago@dev.com", "Магомед Новый")
export async function syncAndGetPublicProfile(email: string, updatedName: string) {
  const safeUserData = await prisma.user.upsert({
    where: { email: email },
    update: { name: updatedName },
    create: {
      email: email,
      name: updatedName,
      age: 25,
    },
    select: {
      id: true,
      name: true,
      email: true, // Забираем только публичные данные, игнорируя даты, роли и блокировки
    },
  });

  return safeUserData;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ (JSON):
    ========================================================================
    {
      "id": "user-uuid-777",
      "name": "Магомед Новый",
      "email": "mago@dev.com"
    }
  */
}


// =========================================================================
// ПРИМЕР 4: Вложенный upsert один-к-одному (Управление профилем через User)
// =========================================================================
// Логика: Повышаем сложность. Мы делаем метод 'update' для пользователя, но внутри 
// связи 'profile' вызываем 'upsert'. 
// Сценарий: Юзер обновляет настройки. Мы не знаем, заполнил ли он блок "О себе" 
// (таблица Profile) ранее. Если связь `profile` уже была — она обновится. Если нет — создастся.

// Пример вызова: const result = await updateUserProfileBio("user-uuid-777", "Fullstack Dev, Dubai")
export async function updateUserProfileBio(userId: string, newBio: string) {
  const userWithProfile = await prisma.user.update({
    where: {
      id: userId, // Находим существующего юзера по его ID
    },
    data: {
      profile: {
        upsert: {
          // Критический нюанс: для связи 1-к-1 блок 'where' внутри вложенного upsert оставляем ПУСТЫМ.
          // Prisma сама знает, какой userId нужно искать, благодаря родительскому контексту.
          where: {}, 
          update: {
            bio: newBio, // Если профиль уже существовал — меняем bio
          },
          create: {
            bio: newBio,  // Если профиля не было — создаем физическую строку
            ownerid: 1,   // Обязательное поле твоей схемы Profile
          },
        },
      },
    },
    include: {
      profile: true, // Сразу подтягиваем изменения для проверки
    },
  });

  return userWithProfile;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ (ПРОФИЛЬ СОЗДАЛСЯ С НУЛЯ ВНУТРИ СВЯЗИ) (JSON):
    ========================================================================
    {
      "id": "user-uuid-777",
      "email": "mago@dev.com",
      "profile": {
        "id": "profile-uuid-abc",
        "bio": "Fullstack Dev, Dubai",
        "ownerid": 1,
        "userId": "user-uuid-777" // Связь проставилась автоматически!
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 5: Вложенный точечный upsert один-ко-многим (Работа с конкретным Постом)
// =========================================================================
// Логика: Максимальный уровень сложности для вложенных мутаций. Мы обновляем Юзера, 
// проваливаемся в массив его постов (`posts`), и делаем `upsert` для одного конкретного 
// поста, зная его ID. Если этот пост существует — правим заголовок, если нет — создаем.

// Пример вызова: const result = await upsertUserPost("user-uuid-777", "post-id-100", "Новая Архитектура")
export async function upsertUserPost(userId: string, postId: string, targetTitle: string) {
  const userWithPosts = await prisma.user.update({
    where: { id: userId },
    data: {
      posts: {
        upsert: {
          where: {
            id: postId, // Проверяем существование конкретного поста по его ID
          },
          update: {
            title: targetTitle, // Нашли? Меняем заголовок
          },
          create: {
            id: postId,         // Не нашли? Создаем пост именно с этим ID
            title: targetTitle,
            content: ["Стартовый контент вложенного апсерта"],
          },
        },
      },
    },
    include: {
      posts: {
        where: { id: postId }, // Тянем только этот измененный пост, чтобы не забивать JSON
      },
    },
  });

  return userWithPosts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ (JSON):
    ========================================================================
    {
      "id": "user-uuid-777",
      "email": "mago@dev.com",
      "posts": [
        {
          "id": "post-id-100",
          "title": "Новая Архитектура",
          "authorId": "user-uuid-777"
        }
      ]
    }
  */
}