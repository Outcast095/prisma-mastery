
// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../../src/prisma.js";


// =========================================================================
// ПРИМЕР 1: Метод findFirst (Поиск первой подходящей записи)
// =========================================================================
// Логика: Ищет по абсолютно любым, даже не уникальным полям. Если под условия 
// подходит много записей, он просто вернет самую первую встреченную в таблице.
// Часто используется в связке со скрытой или явной сортировкой orderBy.

// Пример вызова: const user = await getFirstUserByName("Алиса")
async function getFirstUserByName(name: string) {
  const user = await prisma.user.findFirst({
    where: {
      name: name, // Если в БД три "Алисы", вернет ту, которая лежит выше всех
    },
  });
  
  return user;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Возвращает один объект User или null:

    {
      "id": "user-uuid-alice-1",
      "email": "alice@dev.com",
      "name": "Алиса",
      "city": "Москва",
      "createdAt": "2026-01-10T09:00:00.000Z"
    }
  */
}

// =========================================================================
// ПРИМЕР 2: Поиск самого свежего пользователя (findFirst + orderBy)
// =========================================================================
// Логика: Если отсортировать всю таблицу по дате создания в обратном порядке 
// (desc), то самый последний зарегистрированный юзер окажется на самом верху. 
// Метод findFirst заберет именно его.

// Пример вызова: const newestUser = await getNewestUser()
export async function getNewestUser() {
  const user = await prisma.user.findFirst({
    orderBy: {
      createdAt: 'desc', // Сортируем: от самых свежих к старым
    },
  });

  return user;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    {
      "id": "user-uuid-999",
      "email": "fresh-user@dev.com",
      "name": "Илья",
      "createdAt": "2026-06-10T09:30:00.000Z" // Самая свежая дата в базе
    }
  */
}


// =========================================================================
// ПРИМЕР 4: Поиск по нескольким простым условиям (where с двумя полями)
// =========================================================================
// Логика: Ищет пользователя, у которого совпадают сразу два условия. 
// База найдет первого незаблокированного человека, который живет в Дубае.

// Пример вызова: const activeDubaiUser = await getActiveUserFromDubai()
export async function getActiveUserFromDubai() {
  const user = await prisma.user.findFirst({
    where: {
      city: "Дубай",
      isBlocked: false, // Оба условия должны выполняться одновременно
    },
  });

  return user;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    {
      "id": "user-uuid-dubai-7",
      "email": "mago@dev.com",
      "name": "Магомед",
      "city": "Дубай",
      "isBlocked": false
    }
  */
}


// =========================================================================
// ПРИМЕР 5: Поиск самого старшего пользователя (findFirst + orderBy по числу)
// =========================================================================
// Логика: Нам нужно найти человека с самым большим возрастом. 
// Мы сортируем таблицу по полю 'age' в режиме 'desc' (от большего к меньшему). 
// Самый возрастной юзер станет первым в списке, и findFirst его заберет.

// Пример вызова: const oldestUser = await getOldestUser()
export async function getOldestUser() {
  const user = await prisma.user.findFirst({
    orderBy: {
      age: 'desc', // Сортируем возраст по убыванию
    },
  });

  return user;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    {
      "id": "user-uuid-old-44",
      "email": "elder@dev.com",
      "name": "Пётр",
      "age": 63
    }
  */
}


// =========================================================================
// ПРИМЕР 6: Поиск первого юзера с облегченным ответом (findFirst + select)
// =========================================================================
// Логика: Мы ищем первого попавшегося пользователя из Москвы, но нам не нужен 
// весь его огромный объект из базы. Через 'select' мы просим вернуть только 
// его имя и email.

// Пример вызова: const shortData = await getFirstMoscowUserShort()
export async function getFirstMoscowUserShort() {
  const userFields = await prisma.user.findFirst({
    where: {
      city: "Москва",
    },
    select: {
      name: true,
      email: true, // Вырезаем id, пароли, даты создания
    },
  });

  return userFields;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    {
      "name": "Алиса",
      "email": "alice@dev.com"
    }
  */
}