

// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовый findUnique (Поиск по первичному ключу ID)
// =========================================================================
// Логика: Ищет строку по её главному уникальному идентификатору (`@id`). 
// Этот запрос отрабатывает в PostgreSQL мгновенно, так как поиск идет по индексу B-Tree.
// Если запись найдена — возвращается объект, если нет — возвращается строго null.

// Пример вызова: const user = await getUserById("user-uuid-123")
async function getUserById(id: string) {
  const user = await prisma.user.findUnique({
    where: {
      id: id, // Поле 'id' помечено как @id в schema.prisma
    },
  });

  return user;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Если пользователь с таким ID существует, мы получаем его полный объект:

    {
      "id": "user-uuid-123",
      "email": "magomed@dev.com",
      "name": "Магомед",
      "age": 25,
      "city": "Дубай",
      "isBlocked": false,
      "role": "USER",
      "createdAt": "2026-06-09T12:00:00.000Z",
      "updatedAt": "2026-06-09T18:30:00.000Z"
    }

    Если в базе данных нет такого ID, функция вернет:
    null
  */
}


// =========================================================================
// ПРИМЕР 2: findUnique + select (Безопасный поиск по уникальному Email)
// =========================================================================
// Логика: `findUnique` может искать по любому полю, которое в схеме имеет флаг `@unique`.
// В данном случае мы ищем по email. Чтобы не тащить из базы лишние системные данные, 
// мы комбинируем поиск со знакомым инструментом `select`, забирая только публичную инфу.

// Пример вызова: const publicData = await getPublicUserByEmail("mago@dev.com")
async function getPublicUserByEmail(email: string) {
  const userFields = await prisma.user.findUnique({
    where: {
      email: email, // Поле 'email' обязано быть @unique в схеме
    },
    select: {
      name: true,
      city: true,
      role: true, // Вырезаем id, пароли, даты и флаги блокировок
    },
  });

  return userFields;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    База отдаст только запрошенную проекцию полей. Идеально для профиля на фронтенде:

    {
      "name": "Магомед",
      "city": "Дубай",
      "role": "USER"
    }
  */
}


