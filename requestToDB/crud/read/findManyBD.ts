// =========================================================================
// ПРИМЕР 1: Комбинированный findMany (Операторы gte и endsWith)
// =========================================================================
// Логика: Метод findMany возвращает МАССИВ всех записей, подошедших под условия.
// Если ничего не найдено — вернет пустой массив []. Блок 'where' связывает условия 
// через неявное логическое "И" (AND). 
// gte означает "Greater Than or Equal" (Больше или равно), а endsWith фильтрует конец строки.

// Пример вызова: const users = await filterUsersByAgeAndEmail(18, "@gmail.com")
async function filterUsersByAgeAndEmail(minAge: number, emailDomain: string) {
  const users = await prisma.user.findMany({
    where: {
      age: {
        gte: minAge, 
      },
      email: {
        endsWith: emailDomain, 
      },
    },
  });

  return users; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Массив объектов User, у которых возраст >= 18 И почта заканчивается на @gmail.com:

    [
      {
        "id": "user-uuid-111",
        "email": "magomed@gmail.com",
        "name": "Магомед",
        "age": 25,
        "city": "Дубай",
        "createdAt": "2026-06-09T12:00:00.000Z"
      },
      {
        "id": "user-uuid-222",
        "email": "aslan@gmail.com",
        "name": "Аслан",
        "age": 18,
        "city": "Москва",
        "createdAt": "2026-06-09T13:00:00.000Z"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Оператор gt (Строго больше / Greater Than)
// =========================================================================
// Логика: Фильтрует записи по числовому полю, оставляя только те, у которых 
// значение СТРОГО БОЛЬШЕ указанного лимита (само переданное число не включается).

// Пример вызова: const profiles = await getUsersWithHighCoins(100)
async function getUsersWithHighCoins(minCoins: number) {
  const users = await prisma.profile.findMany({
    where: {
      ownerid: { 
        gt: minCoins, 
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Массив профилей, у которых числовой идентификатор ownerid строго больше 100:

    [
      {
        "id": "profile-uuid-aaa",
        "bio": "Fullstack Developer",
        "ownerid": 101, // 101 > 100 (Прошел)
        "userId": "user-uuid-111"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Оператор lt (Строго меньше / Less Than)
// =========================================================================
// Логика: Фильтрует числовые поля, выбирая строки, значения которых СТРОГО МЕНЬШЕ 
// заданного аргумента (граничное число в выборку не входит).

// Пример вызова: const profiles = await getUsersToAlert(50)
async function getUsersToAlert(maxStorageMb: number) {
  const users = await prisma.profile.findMany({
    where: {
      ownerid: {
        lt: maxStorageMb, 
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернутся профили, чьи ownerid находятся в диапазоне строго ниже 50:

    [
      {
        "id": "profile-uuid-bbb",
        "bio": "Бэкенд-стажер",
        "ownerid": 5, // 5 < 50 (Прошел)
        "userId": "user-uuid-222"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Оператор lte (Меньше или равно / Less Than or Equal)
// =========================================================================
// Логика: Мягкий фильтр "снизу". Находит все записи, у которых числовое поле 
// меньше или в точности РАВНО указанному значению.

// Пример вызова: const profiles = await getYouthUsers(25)
async function getYouthUsers(maxAge: number) {
  const users = await prisma.profile.findMany({
    where: {
      ownerid: {
        lte: maxAge, 
      },
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    В выборку попадут элементы, где ownerid меньше 25, включая сам профиль с ownerid: 25:

    [
      {
        "id": "profile-uuid-ccc",
        "bio": "UI/UX Designer",
        "ownerid": 25, // Идеальное совпадение по значению "равно"
        "userId": "user-uuid-333"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Оператор not (Логическое отрицание "НЕ РАВНО")
