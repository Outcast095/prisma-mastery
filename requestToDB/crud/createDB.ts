// =========================================================================
// ПРИМЕР 3: Метод connectOrCreate (Умное создание в связях Многие-ко-многим)
// =========================================================================
// Логика: Самый частый сценарий бэкенда при работе с ТЕГАМИ. 
// Когда юзер создает Пост и указывает тег (например, "postgres"), мы не знаем, 
// есть ли уже такой тег в базе. 
// connectOrCreate работает так: «Если тег с именем "postgres" уже ЕСТЬ в базе — 
// просто привяжи его к посту (connect). Если такого тега НЕТ — создай его (create) 
// и привяжи». Это защищает от ошибок дублирования уникального поля 'name' в модели Tag.

// Пример вызова: const post = await createPostWithSmartTag("Новый пост", ["Контент"], "user-uuid-777", "postgres")
async function createPostWithSmartTag(title: string, content: string[], authorId: string, tagName: string) {
  const newPost = await prisma.post.create({
    data: {
      title,
      content,
      authorId,
      tags: {
        connectOrCreate: {
          // 'where' ищет существующий тег по уникальному полю name
          where: { name: tagName }, 
          // 'create' сработает ТОЛЬКО если 'where' ничего не нашел в базе данных
          create: { name: tagName } 
        }
      }
    },
    include: {
      tags: true // Посмотрим, какой тег в итоге привязался
    }
  });

  return newPost;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Пост создан. В массиве "tags" будет лежать тег. Если "postgres" уже существовал, 
    у него сохранится его старый оригинальный ID и дата создания:

    {
      "id": "post-uuid-new-555",
      "title": "Новый пост",
      "content": ["Контент"],
      "authorId": "user-uuid-777",
      "createdAt": "2026-06-09T15:20:00.000Z",
      
      // Тег успешно прилинкован без падения базы данных по ошибке уникальности
      "tags": [
        {
          "id": "tag-uuid-existing-111", // Взялся id уже существовавшего в базе тега
          "name": "postgres",
          "createdAt": "2026-04-01T08:00:00.000Z" 
        }
      ]
    }
  */
}