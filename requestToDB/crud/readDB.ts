// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// 2. Импортируем пространство имен Prisma (с большой буквы) для работы с типами и ошибками
import { Prisma } from "../../src/generated/prisma/client.js";


// =========================================================================
// ПРИМЕР 1: Метод findUnique (Поиск по уникальному ключу)
// =========================================================================
// Логика: Ищет строго по одному уникальному индексу (@id или @unique). 
// База данных использует готовые индексы, поэтому запрос выполняется мгновенно.
// Возвращает ровно 1 запись или null, если ничего не найдено.

// Пример вызова: const user = await getUserByEmail("magomed@dev.com")
async function getUserByEmail(email: string) {
  const user = await prisma.user.findUnique({
    where: {
      email: email, 
    },
  });
  
  return user;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    В случае успеха вернется один объект пользователя. Если email нет в базе — null:

    {
      "id": "user-uuid-123",
      "email": "magomed@dev.com",
      "name": "Магомед",
      "city": "Дубай",
      "createdAt": "2026-06-09T12:00:00.000Z"
    }
  */
}


// =========================================================================
// ПРИМЕР 2: Метод findFirst (Поиск первой подходящей записи)
// =========================================================================
// Логика: Ищет по абсолютно любым, даже не уникальным полям. Если под условия 
// подходит много записей, он просто вернет самую первую встреченную в таблице.
// Часто используется в связке со скрытой или явной сортировкой orderBy.

// Пример вызова: const user = await getFirstUserByName("Алиса")
async function getFirstUserByName(name: string) {
  const user = await prisma.user.findFirst({
    where: {
      name: name, // Если в БД три "Алисы", вернет ту, которая лежит выше всех
    },
  });
  
  return user;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Возвращает один объект User или null:

    {
      "id": "user-uuid-alice-1",
      "email": "alice@dev.com",
      "name": "Алиса",
      "city": "Москва",
      "createdAt": "2026-01-10T09:00:00.000Z"
    }
  */
}


// =========================================================================
// ПРИМЕР 3: Метод findMany (Выборка массива записей)
// =========================================================================
// Логика: Достает из базы все записи, удовлетворяющие критериям фильтрации.
// Если ни одна запись не подошла, метод НЕ генерирует ошибку, а возвращает пустой массив [].

// Пример вызова: const posts = await getPublishedPosts()
async function getPublishedPosts() {
  const posts = await prisma.post.findMany({ 
    where: {
      published: true, // Выбираем только те посты, которые были опубликованы
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Всегда возвращает массив объектов (даже если там всего 1 элемент):

    [
      {
        "id": "post-uuid-aaa",
        "title": "Пост про Prisma",
        "published": true,
        "authorId": "user-uuid-123"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 4: Метод findUniqueOrThrow (Гарантированный поиск по уникальному ключу)
// =========================================================================
// Логика: Работает как findUnique, но если запись отсутствует в базе, он 
// жестко выбрасывает исключение PrismaClientKnownRequestError с кодом 'P2025'.
// Это избавляет от необходимости писать ручные проверки `if (!user)`.

// Пример вызова: const user = await getUserProfile("user-uuid-123")
async function getUserProfile(userId: string) {
  try {
    const user = await prisma.user.findUniqueOrThrow({
      where: { id: userId },
    });

    return user;
  } catch (error: unknown) { 
    // Проверяем, является ли ошибка системным исключением Prisma Client
    if (error instanceof Prisma.PrismaClientKnownRequestError) {
      // Код 'P2025' означает: "An operation failed because it depends on one or more records that were not found"
      if (error.code === 'P2025') {
        console.log(`Запись с ID ${userId} не найдена в таблице User.`);
        return null; // В Express-роуте здесь обычно вызывают res.status(404)
      }
    }

    // Если ошибка непредвиденная (упала сеть/БД) — прокидываем её дальше
    throw error;
  }

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Если id существует — вернет объект User. Если id нет в базе — сработает catch и вернет null:

    {
      "id": "user-uuid-123",
      "email": "magomed@dev.com",
      "name": "Магомед",
      "role": "USER"
    }
  */
}


// =========================================================================
// ПРИМЕР 5: Метод findFirstOrThrow (Гарантированный поиск первой записи)
// =========================================================================
// Логика: Ищет по любым полям. Если не найдено ни одного совпадения, 
// генерирует ошибку 'P2025'. Идеально для поиска "самого первого/старого" элемента автора.

// Пример вызова: const firstPost = await getFirstAuthorPostOrThrow("user-uuid-123")
async function getFirstAuthorPostOrThrow(authorId: string) {
  try {
    const post = await prisma.post.findFirstOrThrow({
      where: {
        authorId: authorId,
        published: true, 
      },
      orderBy: {
        id: "asc" // Сортировка по возрастанию id, чтобы забрать самую раннюю запись
      }
    });

    return post;
  } catch (error: unknown) {
    if (error instanceof Prisma.PrismaClientKnownRequestError) {
      // Код 'P2025' идентичен и для findFirstOrThrow
      if (error.code === 'P2025') {
        console.log(`У автора с ID ${authorId} не найдено ни одного опубликованного поста.`);
        return null;
      }
    }
    throw error;
  }

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Возвращает самый первый опубликованный объект поста автора:

    {
      "id": "post-uuid-first-1",
      "title": "Приветственный пост",
      "published": true,
      "authorId": "user-uuid-123"
    }
  */
}


// =========================================================================
// ПРИМЕР 6: Метод count (Подсчет количества строк)
// =========================================================================
// Логика: Выполняет SQL-запрос "SELECT COUNT(*)". Принимает блок 'where' 
// для фильтрации строк перед их подсчетом.

// Пример вызова: const count = await getActiveUsersCount()
async function getActiveUsersCount() {
  const totalActive = await prisma.user.count({
    where: {
      isBlocked: false, // Считаем только незаблокированных (активных) пользователей
    },
  });

  return totalActive;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Метод возвращает чистое, изолированное JavaScript-число (number):

    42
  */
}


// =========================================================================
// ПРИМЕР 7: Метод aggregate (Математические агрегации данных)
// =========================================================================
// Логика: Позволяет за один запрос выполнить математические расчеты над колонками таблицы.
// Поддерживает: _avg (среднее), _max (максимум), _min (минимум), _sum (сумма), _count (количество).

// Пример вызова: const stats = await getUserAgeStatistics()
async function getUserAgeStatistics() {
  const stats = await prisma.user.aggregate({
    _avg: {
      age: true, // Посчитать средний возраст пользователей
    },
    _max: {
      age: true, // Найти возраст самого старого пользователя
    },
    _sum: {
      age: true, // Пример: просуммировать возраст всех (или любое другое числовое поле)
    },
  });

  return stats;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    База данных возвращает структурированный объект, где ключами являются 
    запрошенные операции агрегации:

    {
      "_avg": {
        "age": 25.4
      },
      "_max": {
        "age": 60
      },
      "_sum": {
        "age": 1270
      }
    }
  */
}