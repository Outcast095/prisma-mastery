// =========================================================================
// ПРИМЕР 1: Метод delete (Удаление одной записи по уникальному ключу)
// =========================================================================
// Логика: Блок 'where' указывает, какую именно запись нужно стереть. 
// Искать можно СТРОГО по уникальным полям (@id или @unique).
// Особенность Prisma: Метод возвращает полноценный объект, который ТОЛЬКО ЧТО 
// был удален. Это мега-удобно, если перед удалением нужно залогировать данные 
// или отправить прощальное письмо на email юзера.
// ВНИМАНИЕ: Если записи с таким ID нет в базе, метод выбросит ошибку P2025.

// Пример вызова: const deletedUser = await deleteUserById("user-uuid-123")
async function deleteUserById(id: string) {
  const deletedUser = await prisma.user.delete({
    where: {
      id: id,
    },
  });

  return deletedUser;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (УСПЕХ) (JSON):
    ========================================================================
    База данных физически сотрет строку из таблицы "users", но вернет её слепок в JS:

    {
      "id": "user-uuid-123",
      "email": "magomed@dev.com",
      "name": "Магомед",
      "city": "Дубай",
      "role": "USER",
      "createdAt": "2026-06-09T12:00:00.000Z",
      "updatedAt": "2026-06-09T17:00:00.000Z"
    }
  */
}


// =========================================================================
// ПРИМЕР 2: Метод deleteMany (Массовое удаление записей по условию)
// =========================================================================
// Логика: Используется, когда нужно очистить таблицу полностью или удалить 
// группу записей, подходящих под определенный критерий. В блоке 'where' можно 
// использовать абсолютно любые (даже не уникальные) поля.
// КРИТИЧЕСКИЙ НЮАНС 1: Метод возвращает НЕ массив удаленных объектов, а только 
// отчет с количеством стертых строк: { count: X }.
// КРИТИЧЕСКИЙ НЮАНС 2: Если под условия фильтра не подошла ни одна запись, 
// метод НЕ падает в ошибку, а просто возвращает { count: 0 }.

// Пример вызова: const batchResult = await deleteUsersByName("Иван")
async function deleteUsersByName(name: string) {
  const result = await prisma.user.deleteMany({
    where: {
      name: name, // Удаляем вообще всех пользователей, кого зовут "Иван"
    },
  });

  return result;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Все найденные "Иваны" удалены из PostgreSQL. На выходе получаем строго объект-счетчик:

    {
      "count": 12 // Было найдено и стерто ровно 12 записей
    }
  */
}