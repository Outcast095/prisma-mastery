// =========================================================================
// ПРИМЕР 1: Метод update (Обновление одной записи по уникальному ключу)
// =========================================================================
// Логика: Блок 'where' указывает, какую именно запись мы хотим изменить (искать 
// можно СТРОГО по уникальным полям: @id или @unique). В блок 'data' передаются поля, 
// которые нужно перезаписать. 
// Особенность: Метод возвращает обновленный объект со всеми его полями и актуальными данными.
// ВНИМАНИЕ: Если запись с таким ID не найдена, метод выбросит ошибку P2025.

// Пример вызова: const updatedUser = await updateUserEmail("user-uuid-123", "new-magomed@dev.com")
async function updateUserEmail(id: string, newEmail: string) {
  const updatedUser = await prisma.user.update({
    where: {
      id: id, 
    },
    data: {
      email: newEmail,
    },
  });

  return updatedUser;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (УСПЕХ) (JSON):
    ========================================================================
    Поле email обновилось, а поле updatedAt автоматически изменило значение 
    на текущую дату благодаря директиве @updatedAt в схеме:

    {
      "id": "user-uuid-123",
      "email": "new-magomed@dev.com", // Значение успешно изменилось
      "name": "Магомед",
      "city": "Дубай",
      "role": "USER",
      "createdAt": "2026-06-09T12:00:00.000Z",
      "updatedAt": "2026-06-09T18:00:00.000Z" // Время апдейта зафиксировано
    }
  */
}


// =========================================================================
// ПРИМЕР 2: Метод upsert (Обновление или Создание записи / "Smart Insert")
// =========================================================================
// Логика: Атомарная операция "два в одном". Блок 'where' проверяет наличие уникального поля.
// Если запись НАЙДЕНА -> Prisma выполняет блок 'update' (обновляет существующую строку).
// Если запись НЕ НАЙДЕНА -> Prisma выполняет блок 'create' (создает новую запись с нуля).
// Незаменимо для сохранения сессий, логов авторизации или синхронизации внешних API.

// Пример вызова: const userSession = await saveUserSession("mago@dev.com", "192.168.1.10")
async function saveUserSession(email: string, lastLoginIp: string) {
  const user = await prisma.user.upsert({
    where: {
      email: email,
    },
    update: {
      lastIp: lastLoginIp, // Поле обновится, если юзер уже существовал
    },
    create: {
      email: email,
      lastIp: lastLoginIp, // Поля для создания абсолютно нового аккаунта
    },
  });

  return user; // Метод всегда возвращает итоговый объект (созданный или обновленный)

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ (СЦЕНАРИЙ А: ЗАПИСЬ НАЙДЕНА — ПРОИЗОШЕЛ UPDATE) (JSON):
    ========================================================================
    Пользователь существовал, его системные флаги и ID не изменились, но обновился IP:

    {
      "id": "user-existing-uuid-777",
      "email": "mago@dev.com",
      "name": "Магомед",
      "lastIp": "192.168.1.10",
      "createdAt": "2026-05-01T10:00:00.000Z",
      "updatedAt": "2026-06-09T18:05:00.000Z"
    }

    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ (СЦЕНАРИЙ Б: ЗАПИСЬ НЕ НАЙДЕНА — ПРОИЗОШЕЛ CREATE) (JSON):
    ========================================================================
    Пользователя не было. База создала новую строку, сгенерировала UUID и подставила дефолты:

    {
      "id": "user-brand-new-uuid-999",
      "email": "mago@dev.com",
      "name": null,
      "lastIp": "192.168.1.10",
      "createdAt": "2026-06-09T18:05:00.000Z",
      "updatedAt": "2026-06-09T18:05:00.000Z"
    }
  */
}


// =========================================================================
// ПРИМЕР 3: Метод updateMany (Массовое обновление записей по фильтру)
// =========================================================================
// Логика: Используется для группового изменения строк. Блок 'where' работает как 
// обычный поисковый фильтр (можно использовать любые не уникальные поля). 
// Все строки, подошедшие под условия, изменят свои поля на данные из блока 'data'.
// КРИТИЧЕСКИЙ НЮАНС 1: Метод возвращает не измененные объекты, а только объект-счетчик: { count: X }.
// КРИТИЧЕСКИЙ НЮАНС 2: Если под условия 'where' никто не подошел, метод не выдаст ошибку, а вернет { count: 0 }.

// Пример вызова: const batchResult = await updateUsersName("Ахмед", "Ахмед (Неактивен)")
async function updateUsersName(oldName: string, newName: string) {
  const result = await prisma.user.updateMany({
    where: {
      name: oldName,
    },
    data: {
      name: newName,
    },
  });

  return result;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Все найденные пользователи переименованы в базе данных PostgreSQL. 
    На выходе получаем строгий отчет о количестве затронутых строк:

    {
      "count": 5
    }
  */
}


// =========================================================================
// ПРИМЕР 4: Вложенное массовое обновление (Nested updateMany)
// =========================================================================
// Логика: Сложный архитектурный сценарий. Мы обновляем данные самого пользователя 
// (меняем email) и ОДНОВРЕМЕННО проваливаемся в его связанную таблицу постов (posts), 
// чтобы одним махом отправить все его опубликованные посты в архив (published: false).
// Prisma автоматически заворачивает эту операцию в транзакцию на уровне БД.

// Пример вызова: const result = await updateUserAndArchivePosts("user-uuid-123", "mago-new@dev.com")
async function updateUserAndArchivePosts(userId: string, newEmail: string) {
  const updatedUser = await prisma.user.update({
    where: {
      id: userId,
    },
    data: {
      email: newEmail,
      posts: {
        // Запускаем массовое обновление на массиве связанных постов конкретного юзера
        updateMany: {
          where: {
            published: true, 
          },
          data: {
            published: false, 
          },
        },
      },
    },
  });

  return updatedUser;

  /*
    ========================================================================
    ВАЖНЫЙ НЮАНС ДЛЯ ШПАРГАЛКИ (JSON):
    ========================================================================
    Метод выполнит изменения и в таблице User, и в таблице Post. Однако, 
    сам метод update по умолчанию возвращает поля ТОЛЬКО родительской модели. 
    Изменения в массиве постов успешно произошли в базе данных, но в возвращаемом 
    JSON-объекте самого массива постов не будет (если тебе нужно увидеть и их, 
    дополнительно допиши блок include: { posts: true } на уровне корня запроса):

    {
      "id": "user-uuid-123",
      "email": "mago-new@dev.com", // Email успешно изменен
      "name": "Магомед",
      "city": "Дубай",
      "role": "USER",
      "createdAt": "2026-06-09T12:00:00.000Z",
      "updatedAt": "2026-06-09T18:10:00.000Z"
    }
  */
}