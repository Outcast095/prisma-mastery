// =========================================================================
// ПРИМЕР 1: Оператор hasEvery (Содержит каждый из списка)
// =========================================================================
// Логика: Ищет записи, где в массиве content присутствуют ВСЕ переданные слова. 
// Порядок слов в базе не важен, наличие других (лишних) элементов разрешено.
// Пример вызова: const posts = await findPostsByEveryContent(["Node.js", "PostgreSQL"])

async function findPostsByEveryContent(requiredWords: string[]) {
  const posts = await prisma.post.findMany({
    where: {
      content: { 
        hasEvery: requiredWords, 
      },
    },
  });

  return posts; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернется массив объектов Post. Каждый из них гарантированно содержит 
    и "Node.js", и "PostgreSQL" внутри своего массива content:

    [
      {
        "id": "post-uuid-111",
        "title": "Современный стек бэкендера",
        "content": ["Node.js", "PostgreSQL", "Prisma", "TypeScript"], // Прошел, так как есть оба слова
        "published": true,
        "authorId": "user-uuid-abc",
        "createdAt": "2026-06-09T10:00:00.000Z",
        "updatedAt": "2026-06-09T12:30:00.000Z"
      },
      {
        "id": "post-uuid-222",
        "title": "Базы данных для новичков",
        "content": ["PostgreSQL", "Docker", "Node.js"], // Тоже прошел (порядок не имеет значения)
        "published": true,
        "authorId": "user-uuid-xyz",
        "createdAt": "2026-06-08T14:15:00.000Z",
        "updatedAt": "2026-06-08T14:15:00.000Z"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Оператор has (Содержит конкретный элемент)
// =========================================================================
// Логика: Ищет записи, где в массиве content есть хотя бы ОДНО точное 
// совпадение с переданной строкой. Что лежит в массиве помимо нее — не важно.
// Пример вызова: const posts = await findPostsBySingleContent("Node.js")

async function findPostsBySingleContent(singleWord: string) {
  const posts = await prisma.post.findMany({
    where: {
      content: {
        has: singleWord, 
      },
    },
  });

  return posts; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернутся все посты, авторы которых упомянули "Node.js" хотя бы один раз:

    [
      {
        "id": "post-uuid-333",
        "title": "Мини-пост",
        "content": ["Node.js"], // Прошел, элемент присутствует
        "published": true,
        "authorId": "user-uuid-abc",
        "createdAt": "2026-06-09T11:00:00.000Z",
        "updatedAt": "2026-06-09T11:00:00.000Z"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Оператор equals (Точное совпадение массива)
// =========================================================================
// Логика: Ищет строгое, зеркальное соответствие. Любые лишние элементы 
// или нарушение последовательности строк отбросят запись.
// Пример вызова: const posts = await findPostsWithExactContent(["USER", "MODERATOR"])

async function findPostsWithExactContent(exactArray: string[]) {
  const posts = await prisma.post.findMany({
    where: {
      content: {
        equals: exactArray, 
      },
    },
  });

  return posts; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Пройдут только те посты, чей массив контента идентичен переданному аргументу:

    [
      {
        "id": "post-uuid-444",
        "title": "Служебный лог ролей",
        "content": ["USER", "MODERATOR"], // Идеальное совпадение
        "published": false,
        "authorId": "user-uuid-admin",
        "createdAt": "2026-06-07T09:00:00.000Z",
        "updatedAt": "2026-06-07T09:00:00.000Z"
      }
    ]
  */
}


// =========================================================================
// ГРУППА 2: СКАЛЯРНЫЕ ФИЛЬТРЫ СТРОК И ID (SCALAR / STRING FILTERS)
// =========================================================================

// =========================================================================
// ПРИМЕР 4: Оператор contains (Поиск подстроки)
// =========================================================================
// Логика: Ищет указанный текст в любом месте строкового поля. 
// Флаг mode: "insensitive" отключает чувствительность к регистру (Алекс/алекс).
// Пример вызова: const users = await searchUsersByName("Алекс")

async function searchUsersByName(searchQuery: string) {
  const users = await prisma.user.findMany({
    where: {
      name: {
        contains: searchQuery,   
        mode: "insensitive",     
      },
    },
  });

  return users; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернется массив пользователей, у которых подстрока встретилась в любой части имени:

    [
      {
        "id": "user-uuid-1",
        "email": "alex@gmail.com",
        "name": "Алекс", // Точное совпадение
        "city": "Москва",
        "role": "USER"
      },
      {
        "id": "user-uuid-2",
        "email": "sasha@dev.com",
        "name": "Александр", // Подстрока "Алекс" найдена в начале
        "city": "Дубай",
        "role": "USER"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Оператор startsWith (Начинается с...)
// =========================================================================
// Логика: Проверяет символы строго в НАЧАЛЕ строки. Идеально для фильтрации 
// по префиксам городов, номеров или артикулов.
// Пример вызова: const users = await findUsersByCityPrefix("Мос")

async function findUsersByCityPrefix(cityPrefix: string) {
  const users = await prisma.user.findMany({
    where: {
      city: {
        startsWith: cityPrefix, 
        mode: "insensitive",
      },
    },
  });

  return users; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Пользователь из города "Тимошевск" сюда не попадет, а вот из "Москва" — да:

    [
      {
        "id": "user-uuid-1",
        "email": "alex@gmail.com",
        "name": "Алекс",
        "city": "Москва", // Прошел проверку startsWith
        "role": "USER"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 6: Оператор endsWith (Заканчивается на...)
// =========================================================================
// Логика: Проверяет символы строго в КОНЦЕ строки. Стандартное решение 
// для поиска пользователей по определенным почтовым доменам.
// Пример вызова: const users = await findUsersByEmailDomain("@gmail.com")

async function findUsersByEmailDomain(domain: string) {
  const users = await prisma.user.findMany({
    where: {
      email: {
        endsWith: domain, 
        mode: "insensitive",
      },
    },
  });

  return users; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Будут отсеяны корпоративные почты (@dev.com, @yandex.ru) и возвращены только gmail:

    [
      {
        "id": "user-uuid-1",
        "email": "alex@gmail.com", // Заканчивается на @gmail.com
        "name": "Алекс",
        "city": "Москва",
        "role": "USER"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 7: Оператор in (Вхождение в список разрешенных)
// =========================================================================
// Логика: Работает как массив вариантов. Возвращает запись, если её одиночное 
// поле совпадает хотя бы с ОДНИМ из элементов переданного массива.
// Пример вызова: const users = await findUsersBySpecificIds(["uuid-1", "uuid-2"])

async function findUsersBySpecificIds(userIds: string[]) {
  const users = await prisma.user.findMany({
    where: {
      id: {
        in: userIds, 
      },
    },
  });

  return users; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернутся только те пользователи, чьи UUID были явно перечислены в аргументах:

    [
      {
        "id": "uuid-1",
        "email": "user1@dev.com",
        "name": "Иван",
        "city": "Дубай",
        "role": "USER"
      },
      {
        "id": "uuid-2",
        "email": "user2@dev.com",
        "name": "Сергей",
        "city": "Абу-Даби",
        "role": "GUEST"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 8: Оператор notIn (Исключение из списка / Чёрный список)
// =========================================================================
// Логика: Полная противоположность оператору 'in'. Вырезает из результирующей 
// выборки всех сущностей, чьи маркеры (ID) совпали со списком.
// Пример вызова: const cleanUsers = await getActiveUsersExcludingBlacklist(["uuid-banned-1"])

async function getActiveUsersExcludingBlacklist(blacklistedIds: string[]) {
  const users = await prisma.user.findMany({
    where: {
      id: {
        notIn: blacklistedIds, 
      },
    },
  });

  return users; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    База данных вернет всех существующих юзеров, кроме заблокированного "uuid-banned-1":

    [
      {
        "id": "user-uuid-1", // Этого ID нет в черном списке, он прошел
        "email": "alex@gmail.com",
        "name": "Алекс",
        "city": "Москва",
        "role": "USER"
      }
    ]
  */
}