// 1. Импортируем инстанс prisma для выполнения запросов
import { prisma } from "../src/prisma.js";

// =========================================================================
// 1. ТИП: ПОСЛЕДОВАТЕЛЬНЫЕ ТРАНЗАКЦИИ (Sequential Transactions)
// =========================================================================
// Зачем нужно: Когда тебе нужно выполнить несколько запросов пакетом, и если 
// хотя бы один из них упадет, база должна отменить ВСЕ изменения.
// Как работает: Передаем в prisma.$transaction обычный МАССИВ запросов Prisma.
// Особенность: Запросы выполняются строго друг за другом, но ты НЕ МОЖЕШЬ использовать 
// результат первого запроса во втором запросе внутри этого массива.

// Пример вызова: const batchResult = await upgradeUserToPremium("user-uuid-123")
async function upgradeUserToPremium(userId: string) {
  const query1 = prisma.user.update({
    where: { id: userId },
    data: { isPremium: true, role: "USER" },
  });

  const query2 = prisma.post.create({
    data: {
      title: "Системное уведомление",
      content: ["Вам успешно активирован Premium-статус!", "Добро пожаловать в клуб."],
      authorId: userId,
      published: true,
    },
  });

  // Запускаем транзакцию, передавая массив подготовленных запросов
  const [updatedUser, newPost] = await prisma.$transaction([query1, query2]);

  // Возвращаем кортеж (массив результатов)
  return { updatedUser, newPost };

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (УСПЕХ) (JSON):
    ========================================================================
    Если оба запроса успешны, транзакция фиксируется (COMMIT). Мы получаем 
    массив с результатами выполнения каждого запроса по порядку:

    {
      "updatedUser": {
        "id": "user-uuid-123",
        "name": "Магомед",
        "isPremium": true // Статус обновился
      },
      "newPost": {
        "id": "post-sys-777",
        "title": "Системное уведомление",
        "authorId": "user-uuid-123" // Пост создался
      }
    }

    ========================================================================
    ЧТО ПРОИЗОЙДЕТ ПРИ ОШИБКЕ (ОТКАТ / ROLLBACK):
    ========================================================================
    Если у пользователя "user-uuid-123" в таблице нарушены какие-то связи, 
    или сервер БД упал во время создания поста (query2) — Prisma автоматически 
    сделает ROLLBACK. Юзер НЕ станет премиумным в базе данных. Изменения query1 сотрутся.
  */
}


// =========================================================================
// 2. ТИП: ИНТЕРАКТИВНЫЕ ТРАНЗАКЦИИ (Interactive Transactions)
// =========================================================================
// Зачем нужно: Самый гибкий и частый сценарий. Когда логика создания второй 
// записи НАПРЯМУЮ зависит от данных, полученных из первой записи.
// Как работает: Передаем в функцию асинхронный колбэк. Внутрь залетает 
// специальный клиент "tx". ВСЕ запросы внутри пишем СТРОГО через "tx", а не через "prisma"!
// Особенность: Позволяет использовать JS-логику (if/else), читать базу, выкидывать ошибки.

// Пример вызова: const signupResult = await safeRegisterUserWithProfile("mago@dev.com", "Бэкенд-инженер", "Магомед")
async function safeRegisterUserWithProfile(email: string, bio: string, name?: string) {
  try {
    // Активируем интерактивную транзакцию
    const result = await prisma.$transaction(async (tx) => {
      
      // Шаг 1: Создаем пользователя через транзакционный клиент tx
      const user = await tx.user.create({
        data: { email, name, city: "Дубай" },
      });

      // Шаг 2: Внедряем JS-логику. Например, бизнес-проверка: 
      // Не даем регистрировать профили пользователям со спам-доменов
      if (user.email.endsWith("@spam.com")) {
        // Любой throw внутри транзакции принудительно вызывает ROLLBACK в Postgres
        throw new Error("Регистрация почтовых ящиков данного домена временно заблокирована.");
      }

      // Шаг 3: Создаем профиль, используя ID только что созданного пользователя!
      const profile = await tx.profile.create({
        data: {
          bio: bio,
          ownerid: 1,
          userId: user.id, // Магия: связали через id из Шага 1
        },
      });

      // Возвращаем собранные данные наружу из транзакции
      return { user, profile };
    });

    return result;

  } catch (error: any) {
    console.error("Транзакция отклонена! База данных чиста. Причина:", error.message);
    return { error: error.message };
  }

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (УСПЕХ) (JSON):
    ========================================================================
    Обе таблицы синхронно пополнились данными, транзакция закрылась успешно:

    {
      "user": {
        "id": "user-interactive-999",
        "email": "mago@dev.com",
        "name": "Магомед",
        "city": "Дубай",
        "createdAt": "2026-06-09T16:00:00.000Z"
      },
      "profile": {
        "id": "profile-interactive-888",
        "bio": "Бэкенд-инженер",
        "userId": "user-interactive-999" // Идеально связалось
      }
    }

    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ПРИ ОТКАТЕ (ЕСЛИ EMAIL БЫЛ "test@spam.com") (JSON):
    ========================================================================
    Сработает условие if, вызовется throw Error. Шаг 1 полностью сотрется из Postgres, 
    строка с юзером физически исчезнет. Функция вернет безопасный объект ошибки:

    {
      "error": "Регистрация почтовых ящиков данного домена временно заблокирована."
    }
  */
}



/*
Золотые правила интерактивных транзакций для твоей шпаргалки:
Никогда не забывай tx: Если внутри колбэка $transaction ты случайно напишешь await prisma.user.create 
вместо await tx.user.create, этот запрос выполнится вне транзакции. Если транзакция упадет, 
этот ошибочный запрос не откатится.

Транзакции должны быть быстрыми: Не делай внутри транзакции долгих сторонних действий 
(например, отправку письма через Gmail-сервис или тяжелые запросы к чужим API). 
База данных держит строки заблокированными, пока транзакция открыта. 
Сначала делай все сторонние вызовы, а в транзакцию пиши только чистые команды к БД.
*/