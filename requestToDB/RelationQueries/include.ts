
import { prisma } from "../../src/prisma.js";


//⚠️ Важно для шпаргалки: include нельзя использовать в методах, которые обновляют или удаляют записи пачками 
// (updateMany, deleteMany), потому что эти методы возвращают не объекты, а просто число — количество измененных строк 
// (например, { count: 5 }). Подтягивать связи там тупо не к чему.


// =========================================================================
// ПРИМЕР: Подтягивание МНОЖЕСТВЕННЫХ связей (Один-к-одному И Один-ко-многим)
// =========================================================================
// Логика: Достаем пользователя и одной командой забираем его профиль (объект) 
// и все его посты (массив).

// Пример вызова: const userData = await getUserWithProfileAndPosts("user-uuid-123")
async function getUserWithProfileAndPosts(userId: string) {
  const user = await prisma.user.findUnique({
    where: { id: userId },
    // ПРАВИЛЬНО: Все связные поля перечисляем внутри ОДНОГО объекта include
    include: {
      profile: true, // Подтянет связанный объект Profile {} (Связь один-к-одному)
      posts: true,   // Подтянет связанный массив объектов Post[] (Связь один-ко-многим)
    },
  });

  return user; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    
    Обрати внимание на разницу структур: 
    - "profile" вернулся как одиночный ОБЪЕКТ (или null), потому что это связь 1-к-1.
    - "posts" вернулся как МАССИВ объектов, потому что у одного юзера может быть много постов.

    {
      "id": "user-uuid-123",
      "email": "magomed@dev.com",
      "name": "Магомед",
      "age": 25,
      "isEmailVerified": true,
      "city": "Дубай",
      "isBlocked": false,
      "isPremium": true,
      "role": "USER",
      "status": "EMPLOYED",
      "createdAt": "2026-06-04T12:00:00.000Z",
      "updatedAt": "2026-06-04T14:30:00.000Z",
      
      // Появился благодаря profile: true (Одиночный объект)
      "profile": {
        "id": "profile-uuid-999",
        "bio": "Fullstack Developer.",
        "ownerid": 1,
        "userId": "user-uuid-123"
      },

      // Появился благодаря posts: true (Массив объектов)
      "posts": [
        {
          "id": "post-uuid-aaa",
          "title": "Мой первый пост про Prisma",
          "content": ["Prisma — это круто!", "ORM нового поколения."],
          "published": true,
          "authorId": "user-uuid-123",
          "createdAt": "2026-06-04T13:00:00.000Z",
          "updatedAt": "2026-06-04T13:00:00.000Z"
        },
        {
          "id": "post-uuid-bbb",
          "title": "Изучаю базы данных",
          "content": ["PostgreSQL отлично сочетается с Node.js."],
          "published": false, // Черновик тоже подтянулся, так как мы не ставили фильтр where
          "authorId": "user-uuid-123",
          "createdAt": "2026-06-04T14:15:00.000Z",
          "updatedAt": "2026-06-04T14:15:00.000Z"
        }
      ]
    }
  */
}


// =========================================================================
// ПРИМЕР 2: Использование include при СОЗДАНИИ (create)
// =========================================================================
// Логика: Создаем новый пост и сразу же возвращаем его вместе с данными автора,
// чтобы на фронтенде мгновенно отобразить имя создателя без повторных запросов.

// Пример вызова: const newPostData = await createPostAndGetAuthor("Новый пост", ["Текст..."], "user-uuid-123")
async function createPostAndGetAuthor(title: string, content: string[], authorId: string) {
  const newPost = await prisma.post.create({
    data: {
      title,
      content,
      authorId,
    },
    // МАНЕВР: К свежесозданному посту сразу прицепляем данные автора из таблицы users
    include: {
      author: true, 
    },
  });

  return newPost; 

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    База данных успешно создаст запись в таблице "posts", сгенерирует ей UUID 
    и вернет объект поста, доукомплектованный объектом автора:

    {
      "id": "post-new-uuid-777",
      "title": "Новый пост",
      "content": ["Текст..."],
      "published": false, // По дефолту из схемы
      "authorId": "user-uuid-123",
      "createdAt": "2026-06-04T15:00:00.000Z",
      "updatedAt": "2026-06-04T15:00:00.000Z",
      
      // Объект автора, который подтянулся в момент создания поста благодаря include
      "author": {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "age": 25,
        "isEmailVerified": true,
        "city": "Дубай",
        "isBlocked": false,
        "isPremium": true,
        "role": "USER",
        "status": "EMPLOYED",
        "createdAt": "2026-05-10T10:00:00.000Z",
        "updatedAt": "2026-06-04T14:30:00.000Z"
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 2: Использование include при ОБНОВЛЕНИИ (update)
// =========================================================================
// Логика: Обновляем заголовок поста и хотим сразу же получить обновленный пост 
// вместе со всеми его тегами, чтобы обновить UI в интерфейсе.

// Пример вызова: const updatedPostData = await updatePostTitleAndGetTags("post-uuid-aaa", "Новое крутое название")
async function updatePostTitleAndGetTags(postId: string, newTitle: string) {
  const updatedPost = await prisma.post.update({
    where: { id: postId },
    data: { title: newTitle },
    // МАНЕВР: Изменили поле и сразу вытащили массив связанных тегов (многие-ко-многим)
    include: {
      tags: true, 
    },
  });

  return updatedPost;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Поле "title" изменится в базе, а в ответе мы увидим обновленный пост 
    и массив тегов "tags", которые привязаны к этому посту:

    {
      "id": "post-uuid-aaa",
      "title": "Новое крутое название", // Поле успешно обновилось
      "content": ["Prisma — это круто!", "ORM нового поколения."],
      "published": true,
      "authorId": "user-uuid-123",
      "createdAt": "2026-06-04T13:00:00.000Z",
      "updatedAt": "2026-06-04T15:05:00.000Z", // Дата обновилась автоматически благодаря @updatedAt
      
      // Массив тегов, подтянутый через include в момент апдейта поста
      "tags": [
        {
          "id": "tag-uuid-postgres",
          "name": "postgres",
          "createdAt": "2026-04-01T08:00:00.000Z",
          "updatedAt": "2026-04-01T08:00:00.000Z"
        },
        {
          "id": "tag-uuid-prisma",
          "name": "prisma-orm",
          "createdAt": "2026-04-02T09:15:00.000Z",
          "updatedAt": "2026-04-02T09:15:00.000Z"
        }
      ]
    }
  */
}


// =========================================================================
// ПРИМЕР 3: Использование include при УДАЛЕНИИ (delete)
// =========================================================================
// Логика: Удаляем пост из базы данных, но хотим получить полный снимок (snapshot) 
// удаленной записи вместе с автором, чтобы, например, отправить уведомление или записать в логи.

// Пример вызова: const deletedPostSnapshot = await deletePostAndGetSnapshot("post-uuid-bbb")
async function deletePostAndGetSnapshot(postId: string) {
  const deletedPost = await prisma.post.delete({
    where: { id: postId },
    // МАНЕВР: Перед физическим удалением строки Prisma вытащит связанные сущности
    include: {
      author: true,
    },
  });

  return deletedPost;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Записи в базе данных больше нет, но функция возвращает нам объект-слепок 
    того, что именно было удалено, включая вложенного автора:

    {
      "id": "post-uuid-bbb",
      "title": "Изучаю базы данных",
      "content": ["PostgreSQL отлично сочетается с Node.js."],
      "published": false,
      "authorId": "user-uuid-123",
      "createdAt": "2026-06-04T14:15:00.000Z",
      "updatedAt": "2026-06-04T14:15:00.000Z",
      
      // Данные автора удаленного поста, доступные благодаря include
      "author": {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "role": "USER",
        "status": "EMPLOYED"
        ...
      }
    }
  */
}