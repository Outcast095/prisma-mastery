

import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 1: Базовый select (Облегченный список пользователей для админки)
// =========================================================================
// Логика: Запрашиваем список всех пользователей, но берем только базовые поля 
// для таблицы (id, name, email, role). Все остальные поля (даты, флаги блокировок, 
// возраст, город) база данных даже не будет читать из памяти, что экономит кучу ресурсов.

// Пример вызова: const usersList = await getShortUsersList()
async function getShortUsersList() {
  const users = await prisma.user.findMany({
    select: {
      id: true,
      name: true,
      email: true,
      role: true, // Выбираем только эти 4 поля
    },
  });

  return users;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Вернется массив объектов. Обрати внимание: в JSON нет полей age, city, 
    isBlocked, createdAt и т.д. Объект содержит строго то, что мы попросили:

    [
      {
        "id": "user-uuid-123",
        "email": "magomed@dev.com",
        "name": "Магомед",
        "role": "USER"
      },
      {
        "id": "user-uuid-456",
        "email": "admin@dev.com",
        "name": "Аслан",
        "role": "ADMIN"
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Вложенный select (Пост с минималистичной инфой об авторе)
// =========================================================================
// Логика: Ищем один конкретный пост для страницы чтения. Нам нужен заголовок, 
// контент, а из автора — ТОЛЬКО его имя и город (чтобы скрыть приватный email или роль).
// ПРАВИЛО: Для связей внутри select мы просто открываем еще один вложенный select.

// Пример вызова: const postData = await getPostForReader("post-uuid-aaa")
async function getPostForReader(postId: string) {
  const post = await prisma.post.findUnique({
    where: { id: postId },
    select: {
      title: true,
      content: true, // Берем массив строк контента из самого поста
      author: {
        // Заходим в связанную модель User и выбираем поля для нее
        select: {
          name: true,
          city: true, 
        },
      },
    },
  });

  return post;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    В случае успеха вернется один объект поста. Поля id поста и authorId 
    отсутствуют, так как мы их не указали в select. Объект автора внутри 
    максимально безопасен и очищен от лишних данных:

    {
      "title": "Мой первый пост про Prisma",
      "content": ["Prisma — это круто!", "ORM нового поколения."],
      
      // Появился благодаря вложенному select внутри поля author
      "author": {
        "name": "Магомед",
        "city": "Дубай"
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 3: select + Фильтрация и Сортировка (Профиль юзера и его лучшие посты)
// =========================================================================
// Логика: Самый мощный сценарий. Мы вытаскиваем имя пользователя. Внутри связи 
// "posts" мы одновременно: 
// 1. Фильтруем (where) — берем только опубликованные.
// 2. Сортируем (orderBy) — от свежих к старым.
// 3. Обрезаем поля (select) — забираем только title поста, игнорируя тяжелый контент.

// Пример вызова: const publicProfile = await getUserPublicProfile("user-uuid-123")
async function getUserPublicProfile(userId: string) {
  const profile = await prisma.user.findUnique({
    where: { id: userId },
    select: {
      name: true, // Из самого юзера берем только имя
      posts: {
        where: { published: true }, // Фильтр связи
        orderBy: { createdAt: "desc" }, // Сортировка связи
        select: {
          id: true,
          title: true, // Из постов берем ТОЛЬКО id и заголовок для ссылок
        },
      },
    },
  });

  return profile;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Идеальная структура для рендеринга страницы профиля. В массиве "posts" 
    лежат облегченные объекты без тяжелого массива "content", что экономит 
    память сервера и трафик сети:

    {
      "name": "Магомед",
      
      // Отфильтрованный, отсортированный и обрезанный массив постов
      "posts": [
        {
          "id": "post-uuid-aaa",
          "title": "Мой первый пост про Prisma"
        },
        {
          "id": "post-uuid-ccc",
          "title": "Планы на переезд в Дубай"
        }
      ]
    }
  */
}


// =========================================================================
// ПРИМЕР 4: select в связи Многие-ко-многим (Посты со списком только имён тегов)
// =========================================================================
// Логика: Выгружаем посты для главной страницы. Нам нужны id и title поста, 
// а из привязанных тегов — ИСКЛЮЧИТЕЛЬНО их имена (поле name). Даты создания 
// самих тегов и их ID нам на фронтенде для простого отображения не нужны.

// Пример вызова: const postsWithTags = await getPostsWithTagNames()
async function getPostsWithTagNames() {
  const posts = await prisma.post.findMany({
    select: {
      id: true,
      title: true,
      tags: {
        // Заходим в связанную через таблицу-линковщик модель Tag
        select: {
          name: true, // Забираем только строковое имя тега
        },
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Каждая запись содержит массив объектов тегов, но очищенный от служебных 
    полей БД (id, createdAt, updatedAt):

    [
      {
        "id": "post-uuid-aaa",
        "title": "Мой первый пост про Prisma",
        
        // Появился благодаря select внутри связи tags
        "tags": [
          { "name": "postgres" },
          { "name": "prisma-orm" }
        ]
      },
      {
        "id": "post-uuid-bbb",
        "title": "Изучаю базы данных",
        "tags": [
          { "name": "postgres" }
        ]
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 5: Многоуровневый вложенный select / Матрёшка (Пост -> Автор -> Профиль)
// =========================================================================
// Логика: Достаем один пост. Из поста берем title. Из автора поста (User) берем name. 
// А из профиля автора (Profile) берем bio (биографию). 
// Мы пробиваемся сквозь две связи: Post ➔ User ➔ Profile, вытаскивая строго по одному полю.

// Пример вызова: const deepData = await getPostWithAuthorBio("post-uuid-aaa")
async function getPostWithAuthorBio(postId: string) {
  const post = await prisma.post.findUnique({
    where: { id: postId },
    select: {
      title: true, // Уровень 1: Поле из Post
      author: {
        select: {
          name: true, // Уровень 2: Поле из User
          profile: {
            select: {
              bio: true, // Уровень 3: Поле из Profile
            },
          },
        },
      },
    },
  });

  return post;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Идеально собранный, чистый объект без единого лишнего ID или даты:

    {
      "title": "Мой первый пост про Prisma",
      "author": {
        "name": "Магомед",
        "profile": {
          "bio": "Fullstack Developer. Специализируюсь на React, Node.js и PostgreSQL."
        }
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 6: Использование select при СОЗДАНИИ (Безопасная регистрация юзера)
// =========================================================================
// Логика: Когда пользователь регистрируется, метод prisma.user.create по дефолту 
// возвращает ОГРОМНЫЙ объект со всеми дефолтными флагами (isBlocked, role, isPremium и т.д.).
// Чтобы сразу отдать в роут Express чистый и безопасный ответ, мы пишем select прямо в create.

// Пример вызова: const newUser = await registerNewUser("test@dev.com", "Иван")
async function registerNewUser(email: string, name: string) {
  const createdUser = await prisma.user.create({
    data: {
      email,
      name,
    },
    // МАНЕВР: База создаст пользователя со всеми дефолтами, но вернет в JS только это:
    select: {
      id: true,
      email: true,
      createdAt: true, 
    },
  });

  return createdUser;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    В переменной созданного юзера не будет никаких системных полей. 
    Только то, что нужно для подтверждения успешной регистрации:

    {
      "id": "user-registered-uuid-555",
      "email": "test@dev.com",
      "createdAt": "2026-06-04T16:05:00.000Z"
    }
  */
}