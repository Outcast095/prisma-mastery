import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 2: Оператор связи some (Хотя бы один связанный элемент)
// =========================================================================
// Логика: Фильтрует родительские записи (посты) на основе контента в связях (тегах).
// «Найди посты, у которых ХОТЯ БЫ ОДИН тег соответствует условию».

// Пример вызова: const postsWithTag = await findPostsByTagName("postgres")
async function findPostsByTagName(tagName: string) {
  const posts = await prisma.post.findMany({
    where: {
      tags: {
        some: {
          name: tagName, // Ищет посты, где среди тегов есть тег с именем "postgres"
        },
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Функция вернет массив стандартных объектов Post, которые привязаны к тегу "postgres".
    Сам массив тегов внутрь объектов по дефолту не подмешивается (для этого нужен include):

    [
      {
        "id": "post-uuid-aaa",
        "title": "Мой первый пост про Prisma",
        "content": ["Prisma — это круто!"],
        "published": true,
        "authorId": "user-uuid-123",
        "createdAt": "2026-06-09T13:00:00.000Z",
        "updatedAt": "2026-06-09T13:00:00.000Z"
      }
    ]
  */
}