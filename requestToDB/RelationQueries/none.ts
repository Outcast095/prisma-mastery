import { prisma } from "../../src/prisma.js";


// =========================================================================
// ПРИМЕР 3: Оператор связи none (Ни один из связанных элементов)
// =========================================================================
// Логика: Фильтрует родительские записи. «Найди посты, у которых НИ ОДИН тег 
// не соответствует условию». Используется для черных списков и исключения контента.

// Пример вызова: const cleanPosts = await findPostsWithoutBannedTags(["spam", "flood"])
async function findPostsWithoutBannedTags(bannedTagNames: string[]) {
  const posts = await prisma.post.findMany({
    where: {
      tags: {
        none: {
          name: {
            in: bannedTagNames, // Найдет посты, у которых вообще НЕТ тегов "spam" или "flood"
          },
        },
      },
    },
  });

  return posts;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    В массив попадут только те посты, у которых либо вообще нет тегов, 
    либо их теги не пересекаются со словами "spam" и "flood":

    [
      {
        "id": "post-uuid-bbb",
        "title": "Изучаю базы данных",
        "content": ["PostgreSQL отлично сочетается с Node.js."],
        "published": false,
        "authorId": "user-uuid-123",
        "createdAt": "2026-06-09T14:15:00.000Z",
        "updatedAt": "2026-06-09T14:15:00.000Z"
      }
    ]
  */
}