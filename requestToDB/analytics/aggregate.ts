/*


Метод aggregate в Prisma — это инструмент для выполнения математических и статистических 
расчетов над записями в базе данных.
Вместо того чтобы выкачивать тысячи строк из PostgreSQL в память Node.js и считать всё вручную 
через циклы или метод .reduce(), ты перекладываешь эту работу на саму базу данных. Она выполняет 
расчет на своей стороне мгновенно и возвращает тебе один готовый объект с цифрами.


Зачем он нужен?
Экономия памяти и трафика: Если в таблице 1 000 000 пользователей, тебе не нужно грузить их в приложение, 
чтобы узнать средний возраст. База данных выдаст тебе одно число (например, 25.4), 
сэкономив мегабайты трафика.

Создание дашбордов и аналитики: Идеально подходит для вывода быстрых метрик в админку 
или на главный экран приложения: общая сумма продаж, количество активных юзеров, 
дата последней регистрации.


Скорость работы бэкенда: Базы данных (PostgreSQL, MySQL) под капотом оптимизированы 
для математических операций над индексами. Они делают эти расчеты в десятки раз быстрее, 
чем код на JavaScript.


Что он умеет делать?
Внутри aggregate живут 5 понятных математических функций:

_count — считает количество строк (сколько всего элементов).

_sum — складывает числа (сумма всех кошельков/покупок).

_avg — находит среднее арифметическое (средний чек, средний возраст).

_min — находит самый маленький элемент (минимальная цена, самая ранняя дата).

_max — находит самый большой элемент (максимальный возраст, свежий пост).

💡 Главное отличие от других методов: aggregate всегда «схлопывает» всю выборку строк 
в один-единственный результирующий объект.


*/



// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";


// =========================================================================
// ПРИМЕР 4: Только сумма значений (_sum)
// =========================================================================
// Логика: Извлекаем только сумму всех значений в указанной колонке. 
// База данных просто сложит числа из поля "age" всех строк вместе.

// Пример вызова: const totalSum = await getTotalAgeSum()
export async function getTotalAgeSum() {
  const result = await prisma.user.aggregate({
    _sum: {
      age: true, // Складываем только значения в поле age
    },
  });

  return result;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    {
      "_sum": {
        "age": 4275
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 5: Только минимальное значение (_min)
// =========================================================================
// Логика: Противоположность оператора _max. Нам нужно узнать возраст 
// самого молодого пользователя в таблице. База вернет одно наименьшее число.

// Пример вызова: const minAge = await getMinAge()
export async function getMinAge() {
  const result = await prisma.user.aggregate({
    _min: {
      age: true, // Ищем минимальное значение только для поля age
    },
  });

  return result;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    {
      "_min": {
        "age": 18
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 6: Подсчет строк с простым фильтром (_count + where)
// =========================================================================
// Логика: Делаем операцию _count еще более полезной. Мы добавляем блок 'where', 
// чтобы посчитать количество пользователей не во всей базе, а только в одном городе.

// Пример вызова: const dubaiUsers = await getDubaiUsersCount()
export async function getDubaiUsersCount() {
  const result = await prisma.user.aggregate({
    where: {
      city: "Дубай", // Сначала фильтруем строки по городу
    },
    _count: {
      _all: true,   // Затем считаем только те строки, которые подошли
    },
  });

  return result;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    {
      "_count": {
        "_all": 15
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 1: Только подсчет строк (_count)
// =========================================================================
// Логика: Самый простой вариант агрегации. Мы не смешиваем математику, 
// а просто просим базу данных посчитать общее количество строк в таблице.

// Пример вызова: const totalUsers = await getTotalUsersCount()
export async function getTotalUsersCount() {
  const result = await prisma.user.aggregate({
    _count: {
      _all: true, // Просто считаем все строки подряд
    },
  });

  return result;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    {
      "_count": {
        "_all": 150
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 2: Только средний возраст (_avg)
// =========================================================================
// Логика: Извлекаем только одну метрику — среднее арифметическое значение 
// по колонке "age". База сама сложит возраст всех юзеров и разделит на их количество.

// Пример вызова: const averageAge = await getAverageAge()
export async function getAverageAge() {
  const result = await prisma.user.aggregate({
    _avg: {
      age: true, // Считаем среднее только для поля age
    },
  });

  return result;

  /*
    ========================================================================
    ОЖИДАЕМЫED РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    Результат всегда возвращается в виде дробного числа (Float):

    {
      "_avg": {
        "age": 28.5
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 3: Только максимальный возраст (_max)
// =========================================================================
// Логика: Нам нужно узнать возраст самого старого пользователя в системе. 
// База данных быстро пробежится по колонке "age" и вернет одно максимальное число.

// Пример вызова: const maxAge = await getMaxAge()
export async function getMaxAge() {
  const result = await prisma.user.aggregate({
    _max: {
      age: true, // Ищем максимальное значение только для поля age
    },
  });

  return result;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    {
      "_max": {
        "age": 63
      }
    }
  */
}


// =========================================================================
// ПРИМЕР 1: Комбо-агрегация по всей таблице (Глобальная статистика)
// =========================================================================
// Логика: Мы можем замиксовать любые операторы агрегации в один запрос. 
// Здесь мы за один поход в базу узнаем: общее число юзеров, их средний возраст, 
// а также возраст самого молодого и самого старого пользователя.

// Пример вызова: const globalStats = await getGlobalUserStats()
export async function getGlobalUserStats() {
  const stats = await prisma.user.aggregate({
    _count: {
      _all: true, // Сколько всего строк в таблице
    },
    _avg: {
      age: true,  // Средний возраст по больнице
    },
    _min: {
      age: true,  // Самый молодой
    },
    _max: {
      age: true,  // Самый возрастной
    },
  });

  return stats;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    База возвращает один объект, в котором лежат запрошенные ключи:

    {
      "_count": { "_all": 150 },
      "_avg": { "age": 28.5 },
      "_min": { "age": 18 },
      "_max": { "age": 63 }
    }
  */
}


// =========================================================================
// ПРИМЕР 2: Агрегация с фильтрацией (where + aggregate)
// =========================================================================
// Логика: Блок 'where' ограничивает выборку строк ДО того, как начнется 
// математический просчет. 
// Задача: Посчитать демографическую аналитику строго по активным пользователям из Дубая.
// Используем реальное поле 'age', так как оно гарантированно существует в схеме.

// Пример вызова: const dubaiMetrics = await getDubaiDemographics()
export async function getDubaiDemographics() {
  const metrics = await prisma.user.aggregate({
    where: {
      city: "Дубай",       // Фильтр по городу
      isBlocked: false,   // Только живые аккаунты
    },
    _sum: {
      age: true,          // Суммарный возраст всех пользователей в Дубае
    },
    _avg: {
      age: true,          // Средний возраст дубайского пользователя
    },
    _count: {
      _all: true,         // Сколько именно человек попало в этот расчет
    }
  });

  return metrics;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    {
      "_sum": { "age": 375 },
      "_avg": { "age": 25.0 },
      "_count": { "_all": 15 }
    }
  */
}


// =========================================================================
// ПРИМЕР 3: Критический нюанс Middle-уровня (Агрегация пустой выборки)
// =========================================================================
// Логика: Что произойдет, если под условия 'where' не подошла ни одна строка?
// Например, мы ищем статистику по несуществующему городу "Марс".
// Твой код на Node.js не должен упасть в ошибку, но нужно знать, что вернет база.

// Пример вызова: const emptyStats = await getEmptySelectionStats()
export async function getEmptySelectionStats() {
  const stats = await prisma.user.aggregate({
    where: {
      city: "Марс", // Заведомо пустой результат
    },
    _count: {
      _all: true,
    },
    _sum: {
      age: true,
    },
    _avg: {
      age: true,
    }
  });

  return stats;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    ВНИМАНИЕ: Свойство `_count` ВСЕГДА вернет число 0.
    Но математические операторы `_sum`, `_avg`, `_min`, `_max` вернут строго NULL!
    Потому что нельзя высчитать среднее или сумму у того, чего не существует.

    {
      "_count": { "_all": 0 },
      "_sum": { "age": null },
      "_avg": { "age": null }
    }
  */
}