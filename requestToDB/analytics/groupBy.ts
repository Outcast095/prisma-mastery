import { prisma } from "../../src/prisma.js";


// =========================================================================
// ПРИМЕР 1: Самый базовый groupBy (Просто считаем людей по городам)
// =========================================================================
// Логика: Разделяем всю таблицу на кучки по городам и просто считаем, 
// сколько строк (людей) оказалось в каждой кучке. Никакой лишней математики.

// Пример вызова: const usersPerCity = await countUsersByCity()
export async function countUsersByCity() {
  const stats = await prisma.user.groupBy({
    by: ['city'], // Группируем только по городам
    _count: {
      _all: true, // Просто считаем количество строк в каждом городе
    },
  });

  return stats;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    [
      { "city": "Дубай", "_count": { "_all": 15 } },
      { "city": "Москва", "_count": { "_all": 42 } }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Группировка с одной метрикой (Средний возраст по ролям)
// =========================================================================
// Логика: Разделяем пользователей на группы по их системной роли (USER, ADMIN).
// И для каждой роли узнаем только одну вещь — их средний возраст (`_avg`).

// Пример вызова: const ageByRole = await getAverageAgeByRole()
export async function getAverageAgeByRole() {
  const stats = await prisma.user.groupBy({
    by: ['role'], // Группируем по ролям (USER, ADMIN)
    _avg: {
      age: true,  // Вычисляем только средний возраст для каждой группы
    },
  });

  return stats;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    [
      { "role": "USER", "_avg": { "age": 24.5 } },
      { "role": "ADMIN", "_avg": { "age": 36.2 } }
    ]
  */
}


// =========================================================================
// ПРИМЕР 3: Группировка по двум полям (Город + Роль)
// =========================================================================
// Логика: База ищет уникальные комбинации. Например, отдельно посчитает 
