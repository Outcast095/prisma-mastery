// =========================================================================
// ПРИМЕР 4: Простая фильтрация групп через having (Только города-миллионники)
// =========================================================================
// Логика: Нам нужны не все города, а только те, где база насчитала 
// БОЛЬШЕ 10 пользователей. Блок `having` отсекает мелкие группы.

// Пример вызова: const bigCities = await getOnlyBigCities()
export async function getOnlyBigCities() {
  const stats = await prisma.user.groupBy({
    by: ['city'],
    _count: {
      _all: true,
    },
    // Фильтруем готовые группы: оставляем города, где счетчик строк > 10
    having: {
      city: {
        _count: {
          gt: 10, 
        },
      },
    },
  });

  return stats;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ (JSON):
    ========================================================================
    Города, где живет меньше 10 человек, сюда вообще не попадут:

    [
      { "city": "Дубай", "_count": { "_all": 15 } },
      { "city": "Москва", "_count": { "_all": 42 } }
    ]
  */
}


// =========================================================================
// ПРИМЕР 1: Базовый groupBy (Статистика пользователей по городам)
// =========================================================================
// Логика: Нам нужно построить график для аналитики: сколько пользователей живет 
// в каждом конкретном городе, и какой там средний возраст.
// Мы группируем таблицу User по полю 'city'. База сама соберет все уникальные города 
// и посчитает для каждого города количество строк и средний возраст.

// Пример вызова: const cityStats = await getUsersStatsByCity()
async function getUsersStatsByCity() {
  const stats = await prisma.user.groupBy({
    by: ['city'], // Ключевое поле: разделяем пользователей на группы по городам
    _count: {
      _all: true,  // Считаем общее количество пользователей в этой группе (городе)
    },
    _avg: {
      age: true,   // Считаем средний возраст пользователей в этом городе
    },
  });

  return stats;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    База данных вернет массив объектов. Каждому уникальному городу соответствует 
    один объект со своей изолированной статистикой:

    [
      {
        "city": "Дубай",
        "_count": {
          "_all": 15 // В Дубае зарегистрировано 15 юзеров
        },
        "_avg": {
          "age": 25.4 // Их средний возраст ~25 лет
        }
      },
      {
        "city": "Москва",
        "_count": {
          "_all": 42 // В Москве 42 юзера
        },
        "_avg": {
          "age": 29.1 // Их средний возраст ~29 лет
        }
      },
      {
        "city": null, // Юзеры, у которых поле city не заполнено (NULL)
        "_count": {
          "_all": 3
        },
        "_avg": {
          "age": 21.0
        }
      }
    ]
  */
}


// =========================================================================
// ПРИМЕР 2: Продвинутый groupBy + having (Фильтрация посчитанных групп)
// =========================================================================
// Логика: Задача сложнее. Нам нужно найти «самые активные города» для таргетинга.
// Условия:
// 1. Мы берем только тех юзеров, которые НЕ заблокированы (обычный where перед группировкой).
// 2. Группируем их по городам (by: ['city']).
// 3. КРИТИЧЕСКИЙ МАНЕВР: Через блок 'having' мы фильтруем сами ПОЛУЧЕННЫЕ ГРУППЫ. 
//    Нам интересны только те города, где средний возраст строго БОЛЬШЕ 23 лет 
//    И общее количество незаблокированных пользователей в городе строго БОЛЬШЕ 10.
//    (Блок 'having' — это аналог 'where', но он работает только с результатами агрегации).

// Пример вызова: const activeCities = await getPopularCitiesForTargeting()
async function getPopularCitiesForTargeting() {
  const activeCitiesStats = await prisma.user.groupBy({
    by: ['city'],
    where: {
      isBlocked: false, // Шаг 1: Сначала отсекаем забаненных по всей таблице
    },
    _count: {
      _all: true,
    },
    _avg: {
      age: true,
    },
    // Шаг 2: Фильтруем уже собранные и посчитанные группы городов
    having: {
      age: {
        _avg: {
          gt: 23, // Оставить города, где средний возраст > 23
        },
      },
      city: {
        _count: {
          gt: 10, // И где количество живых юзеров > 10 штук
        },
      },
    },
  });

  return activeCitiesStats;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Город Дубай (из Примера 1) прошел оба фильтра having (15 юзеров > 10, а возраст 25.4 > 23).
    А вот условный Берлин, где всего 2 юзера, база данных полностью выбросит из ответа:

    [
      {
        "city": "Дубай",
        "_count": {
          "_all": 15
        },
        "_avg": {
          "age": 25.4
        }
      },
      {
        "city": "Москва",
        "_count": {
          "_all": 42
        },
        "_avg": {
          "age": 29.1
        }
      }
    ]
  */
}


