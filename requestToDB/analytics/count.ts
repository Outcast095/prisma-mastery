// 1. Импортируем инстанс prisma (с маленькой буквы) для выполнения запросов
import { prisma } from "../../src/prisma.js";

// =========================================================================
// ПРИМЕР 6: Метод count (Подсчет количества строк)
// =========================================================================
// Логика: Выполняет SQL-запрос "SELECT COUNT(*)". Принимает блок 'where' 
// для фильтрации строк перед их подсчетом.

// Пример вызова: const count = await getActiveUsersCount()
async function getActiveUsersCount() {
  const totalActive = await prisma.user.count({
    where: {
      isBlocked: false, // Считаем только незаблокированных (активных) пользователей
    },
  });

  return totalActive;

  /*
    ========================================================================
    ОЖИДАЕМЫЙ РЕЗУЛЬТАТ ВЫПОЛНЕНИЯ ФУНКЦИИ (JSON):
    ========================================================================
    Метод возвращает чистое, изолированное JavaScript-число (number):

    42
  */
}